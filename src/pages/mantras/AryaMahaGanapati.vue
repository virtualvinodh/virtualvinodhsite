    <hr/>
    <p>“These Ananda, are the “hearts” of Ganapati”</p>
    <p>“Any son or daughter of high birth, whether monk or nun, lay brother or sister who undertakes any matter [such as] accomplishing the [rites to call a sacred being by means of] spells, worshipping the three ewels, travelling to another country, going to the royal court or concealing [from view] should upon worshipping the blessed Buddha, practice seven times the Arya Ganapati Hrdaya [spells]: for him all tasks will be accomplished; no doubt about this!. He should forever put an end to all strifes and quarrels , violence and envy, and become entirely calm. Day upon day abiding the rules and practicing a full seven times: it will come out into the fortune of this great one! Upon his coming to the royal court there will be great kindness (prasada). He will become a keeper of hearing. There wil be no major illness to his body. Never will he assume the descent as a tara-praksina or the descent as a humble bee: nothing ellse will occur to him that the mind of awakening. In every birth he will be remembering [his previous] births.”</p>
    <p>Thus spoke the Blessed One, and upon receiving [his teaching] these monks, these great bodhisattvas and whole attendance, the world with the gods, the humans, the asuras, the garudas and the gandarvas rejoiced at the words of the Blessed One.</p>
    <p>(Translation adapted from Duquenne)</p>
