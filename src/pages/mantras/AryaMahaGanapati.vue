    <p class="text-h6">The Deity Gaṇapati Hṛdayā</p>
    <p>Interestingly, a female deity named “Gaṇapati Hṛdayā” exists. She is supposedly the prajna-shakti consort of Ganapati. Her form in a text called “Dharmakosha Sangraha” is described as:</p>
    <p class="blockquote">gaṇapatihṛdayā ekamukhā dvibhujā varadā abhayā nṛtyāsanā</p>
    <p class="blockquote">Ganapatihridayaa is one-faced, two-armed, exhibits in her two hands the Varada & Abhaya poses, and shows the dancing attitude
</p>

