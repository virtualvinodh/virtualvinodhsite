    <p class="text-body2">Ganapati, or Ganesha as he is popularly known is a pan Indic deity who is revered in both Hinduism & Buddhism as far as Japan. As in the Brahmanic religion, Buddhism too recognizes Ganapati or Vinayaka as the remover of obstacles. The bijakshara (seed syllable) of Ganapati is “gaḥ“. According to Buddhist legends, he was subdued by Avalokiteshvara. In one Legend, Avalokiteshvara assumes the eleven-headed form and subdues Vinayaka who is causing destruction. He  performs the act by cutting of the head of Vinayaka and placing the head on him, thus taking over Vinayaka's form. In the other Legend, Avalokiteshvara takes the form of a woman to seduce Vinayaka and prevents him from creating obstacles. But, ultimately, both Avalokiteshvara & Ganapati are seen as the manifestation of Buddha Maha Vairochana. Hanguag, a disciple of Amoghavajra, explains that the bijas of Avalokitesvara, "hrīh", & Ganapati, "gaḥ", are an expression of provisional reality.</p>
    <p>There are two sutras on Ganapati that contain dharanis to the deity. The Dharani Sutra of the Golden Ganapati, found in the Chinese Taisho Tripitaka T. XXI 1269, was delivered by the Buddha to his disciple Shariputra when the Buddha was residing at Shravasti. The dharani recorded is very much corrupted. However, another sutra titled "Arya Maha Ganapati Hridaya", contains a complete dharani. At the end of this sutra, Ganapati himself appears and assures that he will protect and bestow wishes of those who uphold this dharani.</p>
    <p> The text of the Arya Maha Ganapati Hridaya is given below:</p>
    <p class="text-h6">Ārya Mahā Gaṇapati Hṛdaya Dhāraṇi Sutra</p>
    <p>namo bhagavate āryamahāgaṇapatihṛdayāya |</p>
    <p>namo ratnatrayāya ||</p>
    <p>Thus I have heard. Upon a time, the Blessed One was staying at Rajagriha, on the Vulture Peak, together with a great assembly of monks: forty-five hundreds of monks and numerous great Bodhisattvas. On that occasion the Blessed One told the Venerable Ananda:</p>
    <p>“Ananda, whoever, son or daughter of high birth, would keep [in mind], recite, obtain and propagate these “heart” [spells] of Ganapati, his will be the accomplishments of all his tasks”</p>
    <q-img src="../../assets/mantras/ganapati-2.png" width="200px" class="float-left q-ma-md"/>
