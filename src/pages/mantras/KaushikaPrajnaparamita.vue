<p class="blockquote">emptiness of the subject, emptiness of the object, emptiness of both subject and object, emptiness of emptiness, the great emptiness, emptiness in the ultimate sense, conditioned emptiness, unconditioned emptiness, absolute emptiness, emptiness without beginning and end, emptiness of non-repudiation, emptiness of essential nature, emptiness of all dharmas, emptiness of own marks, emptiness of the absence of a basis, emptiness of non-existence, emptiness of own-being, emptiness of the non-existence of own being. There are in short the emptiness. This is the perfection of wisdom.</p>
