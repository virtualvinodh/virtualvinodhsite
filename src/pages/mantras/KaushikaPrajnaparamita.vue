Kauśika Prajñāpāramitā Sutra, one of the short Prajnaparamita sutras, is taught to Indra, Kaushika being his epithet, by the Buddha. The Buddha explains the meaning of Prajnaparamita in various different ways, and then summarizes the entire Prajnaparamita into 18 types of emptiness</p>
