<p>Conze considers the words “śruti smṛti mati gati”, as referring to the stages of realization of the Prajnaparamita as in “heard, remembered, understood and arrived at”. This makes mantras more of a mnemonic device to summarize and recollect the process of Prajnaparamita. It is quite possible that initially mantras/dharanis were intended to be mnemnonic devices that later took a life of their own.</p>

<p>I have followed the Conze’s version of the sutra as usual, which contains only the above mantra. He himself had followed the Tibetan recession. He further states that the Central Asian version of the sutra is much longer with additional Mantras in it. The Uwest digital Sanskrit canon probably has this version with the expanded list of mantras.</p>

