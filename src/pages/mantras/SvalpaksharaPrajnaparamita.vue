    <p class="text-body2">
Svalpākṣarā Prajñāpāramitā sutra belongs to the larger Prajnaparamita class of literature. The term <em>svalpakshara</em> could be translated as "few letters”. Consequently, the title can be rendered in English as “Perfection of Wisom in Few Letters”. The text contains two mantras pertaining to the (deified) Prajnaparamita. It appears to be an attempt to make the Prajnaparamita texts more accessible to a less-intellectually inclined lay buddhist practitioner. The text strongly hints that by chanting the mantras a devotee can still attain the same benefits of someone appreciating the metaphyisical pondering of the Ashtasahasrika or Vajracchedika Prajnparamita texts. As such, the text does not want Prajnaparamita to be solely percieved as an intellectual exercise restricted to a select few and risk alienating a significant section of the population.</p>
<p class="blockquote">ye'pi sattvā mandāsvādāḥ, te'pi imāṃ prajñāpāramitāṃ svalpākṣarāṃ dhārayiṣyanti vācayiṣyanti likhiṣyanti likhayiṣyanti, te sarve alpopāyena bodhiparāyaṇā bhaviṣyanti| anena kāraṇena kulaputra iyaṃ saṃkṣiptā svalpākṣarā prajñāpāramitā</p>
<p class="blockquote">Even those beings of dull-taste, [if] they too will hold on to, read and write the Prajnaparamita of few letters, the perusal of Bodhi will happen to them with minimal effort. For this reason, this is the concise Prajnaparamita of few letters </p>
<p>The sutra begins with the Buddha staying at Rajagriha with the host of bhikshus, bodhisattvas & devas. There upon, bodhisattva Avalokiteshvara rises from his seat and requests the Buddha to teach the <em>Prajnaparamita in few letters</em>:
    <p class="blockquote"> deśayatu bhagavān prajñāpāramitāṁ svalpākṣarāṁ mahāpuṇyām, yasyāḥ śravaṇamātreṇa sarvasattvāḥ sarvakarmāvaraṇāni kṣapayiṣyanti, niyataṁ ca bodhiparāyaṇā bhaviṣyanti | ye ca sattvā mantrasādhane udyuktāsteṣāṁ cāvighnena mantrāḥ sidhyanti |</p>
    <p class="blockquote">Demonstrate, Oh Bhagavan, the Svalpakshara Prajnaparamita, which is of great punya, which on merely hearing, the karma obstacles of all the beings will be extinguished, and they will definitlely end up in enlightenment; and the mantras of the beings who labour zealously at the evocation of mantras will succeed without fail.</p>
    <p> The Buddha on hearing Avalokiteshvara’s request, praises him and enters a Samadhi called “sarvaduḥkhapramocana” (liberation of all sufferings), after hacing exhibited several auspicious signs proclaims the Svalpakshara Prajnaparamita:</p>
    <p class="blockquote"> bodhisattvena mahāsattvena samacittena bhavitavyam | sarvasattveṣu maitracittena bhavitavyam | kṛtajñena bhavitavyam | kṛtavedinā ca bhavitavyam | sarvapāpaviratacittena bhavitavyam [...] idaṁ ca prajñāpāramitāhṛdayamāgrahītavyam</p>
    <p class="blockquote">The bodhisattva mahasattva should have an even thought, he shoud have a friendly thought towards all beings, he should be thankful, he should be grateful, and he should desist in his heart from all evil [...] and this Prajnaparamita Hridaya should be repeatedly recited.</p>
