    <hr/>
    <p>(This last part of the above mantra, <em> oṁ mune mune mahāmunaye  </em> also appears in a Prajnaparamita text titled “Vajparani Prajnaparamita Sutra”)
<p>The Buddha further says that he attained anuttara-samyaksambodhi, through this mantra and adds that this mantra was hear from a previous Buddha Maha Shakyamani. He further extols the various benifits of chanting, writing and propogating this mantra. He then invokes another mantra:</p>
    <hr/>
    <p> <transliterate source="IAST" :target="tab"  class="text-body1">tadyathā- oṃ jeya jeya padmābhe avame avame sarasaraṇi . # dhiridhiri devatā anupālani yuddhāttāriṇi paracakranivāriṇi . # pūraya pūraya bhagavati sarva āśā mama ca sarvasattvānāṃ ca . #sarvakarmāvaraṇāni viśodhaya buddhādhiṣṭhite svāhā .</transliterate></p>
    <hr/>
<p>The Buddha further expands: </p>
<p class="blockquote">iyaṁ sā kulaputra paramārthaprajñāpāramitā sarvabuddhānāṁ jananī bodhisattvamātā bodhidātrī pāpahārakā | sarvabuddhairapi na śaknoti asyānuśaṁsā vaktuṁ yāvatkalpakoṭiśatairapi | anayā paṭhitamātreṇa sarvaparṣanmaṇḍalābhiṣiktā bhavanti, sarve ca mantrāḥ abhimukhā bhavanti ||</p>
<p class="blockquote">This kulaputra, is the ultimate Prajnaparamita, the progenitor of all Buddhas, mother of bodhisattvas, giver of bodhi, destroyer of sins. Even all the Buddhas are unable to express in words her advantages, even after hundreds of kotis of aeons. Where it is merely recited, there all the assemblies are consecrated, and all the mantras are realized.</p>
<p>The sutra ends with the usual notes of the benefits of reading the Sutra and the assembly of nagas, yakshara, gandharvas, devas etc finally rejoicing at the Buddha's discourse.
