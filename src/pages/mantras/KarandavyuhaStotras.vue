  <p class="text-h5">Karandavyuha Stotras on Avalokitesvara</p>
          <p><small><i>Composed: May, 2010. Updated: Sep, 2020</i></small></p>

  <q-img src="../../assets/mantras/avalokitesvara.jpg" width="150px" class="float-left q-ma-md" />
    <p class="text-body2">Kāraṇḍavyūha Sutra is an important sutra among the various Mahayana sutras. Supposedly, it is one of the first Sutras that was translated into Tibetan from Sanskrit. According to Tibetan traditions, a casket containing various Mahayana sutras fell from the sky, which also included the Karandavyuha Sutra.  It is wholly devoted to expounding Avalokiteshvara’s qualities and his infinite virtues. Thee sutra also contains one of the most important mantras in Mahayana, namely the Shadakshara (Six Syllabled) Mantra: Oṁ maṇipadme hūṁ. Only through his association with the mantra did Avalokiteshvara attain the title “Shadakshari”.
</p>
<p>In the 12th chapter of the first part of the sutra, Avalokiteshvara visits the palace of a deva in the guise of a begging brahmin. The deva, who is first reluctant to donate to the brahmin, is later encouraged by the auspicious signs that appear inside his house. He then invites the Brahmin into his house, feeds him and provides him with various offerings. The deva asks the Brahmin whether he is a deva or human, for he has not seen any one like him before. Avalokiteshvara replies:
</p>
<p class="blockquote">na devaḥ api tu mānuṣo’haṁ bodhisattvabhūtaḥ | <br/>
evaṁ hīnadīnānukampako bodhimārgamupadarśakaḥ |</p>
<p class="blockquote">Not a deva, but only a human I am,  who has become a Bodhisattva. <br/>
Thus, a sympathizer for lowly-helpless beings and a shower of the Bodhi path.</p>
<p>Thus, reiterating the fact that bodhisattvahood is attainable by everyone</p>
<p>The sutra contains a myriad of deities from the Hindu pantheon praising and venerating Avalokitesvara. We will be seeing a couple of those praises here.</p>
<p class="text-h6">Stotra by Yama</p>
  <q-img src="../../assets/mantras/yama.jpg" width="150px" class="float-left q-ma-md" />

<p>This occurs in the second chapter titled "Drying of Avici". Avalokiteshvara enters the Avici hell to save the beings present there. Upon entering the hell, the lakes of fire dry up and lotuses as large as chariot wheels start to bloom instead, follwed by the appearance of refreshing pools. The guardians gets startled over such strange happenings and report the events to Yama, the god of death. Yama begins to wonder whether the deity that has entered the hell was Narayana, Maheshvara or Ravana. On seeing through his divine eye, he finds Avalokiteshvara instead. He immediately appears before Avalokiteshvara, prostrates and utters the following praises:
    <hr/>
    <p> <transliterate source="IAST" :target="tab"  class="text-body1">namo'stvavalokiteśvarāya, maheśvarāya, padmaśriye, varadāya, vaśaṃkarāya, pṛthivīvaralocanakarāya, jagadāśvāsanakarāya, śatasahasrabhujāya, koṭīśatasahasranetrāya, ekādaśaśīrṣāya, vaḍavāmukhaparyantāya, dharmapriyāya, sarvasattvaparimokṣaṇakarāya, kūrmamakaramatsyāśvāsanakarāya, jñānarāśyuttamakarāya, priyaṃdadāya, ratnaśriye, uttamāya, avīcisaṃśoṣaṇakarāya, jñānalakṣmyalaṃkṛtāya, jñānapriyāya, sarvadevapūjitanamaskṛtāya, vanditāya, abhayaṃdadāya, dharmadīpaṃkarāya, kāmarūpāya, gandharvarūpayā, kāñcanaparvatasamārūḍhāya, sāgarakukṣigambhīradharmāya, paramārthayogamanuprāptāya, saṃmukhasaṃdarśanakarāya, anekasamādhiśatasahasrāvakīrṇāya, abhiratikarāya, vicchuritagātrāya, ṛṣipuṃgavakarāya, haḍinigaḍabandhanabhayatrastamārgaparimokṣaṇakarāya, sarvasattvābhāvasaṃyuktāya, bahuparivārasaṃvartanīyāya, upacitakarāya, cintāmaṇiratnāya, nirvāṇamārgopadarśanakarāya, pretanagarasamucchoṣaṇakarāya, chatrabhūtajagatkarāya, vyādhiparimocanakarāya, nandopanandanāgarājakṛtayajñopavītāya, amoghapāśasaṃdarśanakarāya, anekamantraśatāvakīrṇāya vajrapāṇividrāvaṇakarāya, trilokabhayaṃkarāya, yakṣarākṣasabhūtapretavetālaḍākinīkūṣmāṇḍāpasmārasaṃtrāsanakarāya, nīlotpalacārunetrāya, gambhīradhīrāya, vidyādhipataye, sarvakleśavimokṣaṇakarāya, vividhabodhimārgopacitāya, samārūḍhamokṣamārgapravarāya, āśrayacittabodhimārgopacitāya, pretagatiparimokṣaṇakarāya, paramāṇurajopamasamādhiśatasahasrākīrṇāya</transliterate></p>
    <hr/>
    <p>Yama uses various interesting terms to describe Avalokiteshvara. Note the term, "nandopanandanāgarājakṛtayajñopavīta" – One who wears the sacred Thread made of the naga kings Nanda & Upananda. It should be noted in the larger version of Nilkantha Dharani, Avalokiteshvara is praised as "kṛṣṇasarpakṛtayajñopavīta" (one who wears the sacred thread made of a black snake). He is also interestingly praised as "trilokabhayaṁkara" – terrifier of the three worlds.</p>
    <p class="text-h6"> Stotra by the Asura King Bali </p>
      <q-img src="../../assets/mantras/bali.jpg" width="150px" class="float-left q-ma-md" />

    <p>In the latter part of the sutra, Avalokiteshvara visits the asura king Bali, who is prisoned in an iron palace along with his retinue. He expounds to the king the dharma of giving offerings to the right person. The bodhisattava points out that the offerings given to the Buddha and his sangha surpasses all other offerings, generating innumerable merits. Bali explains to Avalokiteshvara that he has attained his current state by giving offering to the wrong person. Bali narrates the story of Narayana taking the form of a dwarf and tricking him to offer 3 feet of land. Upon receiving his 3 feet, Narayana in his cosmic form measured the entire world with this two feet and asked Bali the promised land for the third feet. As Bali couldn't provide any more land, Narayana demanded that he should reside wherever he might place him to which Bali agrees. He again laments to Avalokitesvara that he has put his offerings at the wrong place.</p>
    <p>Bali, then, recites the following stora on Avalokiteshvara and asks him to be his protector.</p>
    <hr/>
    <p> <transliterate source="IAST" :target="tab"  class="text-body1">trāṇaṃ bhavāhi śubhapadmahasta . padmaśriyālaṃkṛtaśuddhakāya . amitābhamūrte śirasā namāmi . jaṭārdhamadhye cintāmaṇimukuṭadharāya . śubhapadmahastāya . padmaśriyā samalaṃkṛtāya . jaṭāmukuṭadharāya . sarvajñavaśīkṛtāya . bahusattvāśvāsanakarāya . hīnadīnānukampāya . dīvākaravararocanakarāya . pṛthivīvararocanakarāya . bhaiṣajyarājottamāya . suśuddhasattvāya . paramayogamanuprāptāya . mokṣapravarāya mokṣapriyāya . cintāmaṇivatsadṛśāya . dharmagañjaparipālanakarāya . ṣaṭpāramitānirdeśanakarāya . sucetanakarāya</transliterate></p>
    <hr/>
    <p class="blockquote">idaṁ stotraṁ mayā kṛtamavalokiteśvarasya | sukhitāste sattvā ye tava nāmadheyamanusmaranti |
</p>
    <p class="blockquote">This stotra of Avalokiteshvara is created by me (Bali). Comforted are those beings who keep in mind your (Avalokiteshvara's) names.</p>
    <p class="text-h6">Stotra by Maheshvara (Shiva) </p>
      <q-img src="../../assets/mantras/shiva.jpg" width="100px" class="float-left q-ma-md" />

    <p>Towards the end of the sutra, Maheshvara (Shiva) prostrates before the Buddha and requests for a vyākaraṇa (prediction of buddhahood) from him. Shakyamuni receives Maheshvara and instructs him to receive it Avalokiteshvara instead. He approaches Avalokiteshvara, prostrates before him by placing his head on the ground and utters the following praises:</p>
    <hr/>
    <p> <transliterate source="IAST" :target="tab"  class="text-body1">namo'stvalokiteśvarāya maheśvarāya padmadharāya padmāsanāya padmapriyāya śubhapadmahastāya padmaśriye parivṛtāya jagadāsvāsanakarāya pṛthivīvaralocanakarāya prahlādanakarāya</transliterate></p>
    <hr/>
    <p>Maheshvara himself praises Avalokiteshvara with the epithet “Maheshvara”. After hearing the praises, Avalokiteshvara predicts that Maheshvara would in the future become a Buddha named “Bhasmeshvara” in a world system named “vivrita“. </p>
    <p>
    <p class="text-h6"> Stotra by Uma Devi </p>
          <q-img src="../../assets/mantras/umadevi.jpg" width="100px" class="float-left q-ma-md" />

    <p>After Maheshvara has received his Vyakarana, Uma Devi appears before Avalokiteshvara, prostrates before him and praises him as follows:</p>
    <hr/>
    <p> <transliterate source="IAST" :target="tab"  class="text-body1">namo'stvalokiteśvarāya meheśvarāya prāṇaṃdadāya pṛthivīvaralocanakarāya śubhapadmaśriye parivṛtāya nirvāṇabhūmisaṃprasthitāya sucetanakarāya dharmadharāya</transliterate></p>
    <hr/>
    <p>Avalokiteshvara prophesizes that Uma Devi would in the future attain enlightenment and become a Buddha named “Umeshvara”  in a world system south of the Himalayas. The Buddha comments to the bodhisattva Sarvanivaranavishkambhin that just as Avalokiteshvara has converted Uma Devi [and Maheshvara], likewise, he will do it for all the sentient beings.</p>
