<p>Gāyatrī is one of the famous metres in Sanskrit prosody. The mantras composed in gayatri metre are referred to as gayatri mantras. The usual form of the gayatri metre consists of 24 syllables split into 3 metrical feet (pāda) of 8 syllables each. This is known as tripāda gāyatrī. The most famous gayatri mantra is the one present in the Vedas, dedicated to the Sun. However, other deities also have specific mantras composed in the Gayatri metre.

