<p class="text-h6"> Lopamudra </p>
<p>Sanskrit and Tamil have different orthographies. Tamil is a phonemic script while Sanskrit is phonetic, as the Tamil unvoiced plosives have several allophones based on their context. Due to this, people often  apply Tamil reading rules to Sanskrit text and read it as it were Tamil. For instance, one might be tempted to read <span class="tamilextended">เดเดคเตเดฐเตโเดฎเดเตเดเดฆเตเดชเดพเดจเดฟ</span> as /sadurmakuแธadฤซbฤni/ using Tamil reading rules as opposed to /caturmakuแนญadฤซpฤni/ using Sanskrit rules. Therefore, there might be a necessity to mark these consonants explicitly in some Sanskrit texts as a reminder to not alter them based on Tamil phonemic principles. We have attemped to do this by slightly modifying the plosives to explicitly mark them as Sanskrit consonants, as in <span class="lopamudrasans">เดเดคเตเดฐเตโเดฎเดเตเดเดฆเตเดชเดพเดจเดฟ</span></p>

<p>Lopamudra is a variant of the Agastya typeface with these modified forms. Inspired by Grantha forms, we introduce minor changes to <span class="tamilextended">เด เด เด เดค & เดช</span> resulting in the forms <span class="lopamudrasans">เด เด เด เดค & เดช</span>. The modified shapes of  <span class="lopamudrasans demo">เด เด เดค</span> with a small curve below is based on Grantha <span class="grantha">๐</span> and those of <span class="lopamudrasans demo">เด เดช</span> with a small vertical line underneath is based on Grantha <span class="grantha">๐</span>. Also, Agastya, following Tamil script principles, doesn't  distinguish the right part of -au vowel sign and lฬค. For instance, <span class="tamilextended">เดเต</span> /kau/ and <span class="tamilextended">เดเตเดณ</span> /kฤlฬคa/ have the same appearance. Lopamudra additionally tries to remedy this by adopting the corresponding Grantha character for the retroflex letter to visually distinguish them as <span class="lopamudrasans">เดเต</span> and <span class="lopamudrasans">เดเตเดณ</span>. <p>
<p>Lopamudra also attempts to distinguish <span class="tamilextendedss">เดฅ</span> /tha/ and <span class="tamilextendedss">เดง</span> /dha/ from <span class="tamilextendedss">เดฏ</span> /ya/. The variant forms were inspired by the forms appearing in handwritten manuscripts. We reduced the height of /tha/ and /dha/ to make these character a bit more obvious and distinct.</p>
