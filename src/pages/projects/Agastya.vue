<p> To avoid rendering engines (particularly Microsoft products) from treating the text as Malayalam and forming Malayalam-like conjuncts, one needs to insert ZWNJ after the Virama except for the compounds /kแนฃa/ & /ลra/. /ลra/ requires ZWJ to be inserted after the Virama. Therefore, some care needs to be taken when using standard Malayalam input methods to compose using Agastya. Also, modern Malayalam uses only the right part of the -au sign (U+0D57) as in: เดเต, but Agastya/Lopamudra require the two-part vowel (U+0D4C) sign to be used, as in เดเต.</p>
