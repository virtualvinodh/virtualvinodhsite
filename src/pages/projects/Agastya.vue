Agastya is a Unicode fontset consisting of three fonts: <i>Agastya</i>, <i>Lopamudra</i> & <i>Idhmavaha</i>, for Extended Tamil. The script is an experimental attempt to use the full complimentary set of Grantha letters to fill in the gaps in the Tamil script (as compared to the pan-Indic system). All three fonts are available in both sans and serif styles.</p>

<p class="text-h6">Extended Tamil Script</p>
<p>Modern Tamil only adopts /ja/, /ลa/, /แนฃa/, /sa/, /ha/ and the compound /ลrฤซ/ from the Grantha script into its character repertoire. More often, the missing aspirated/voiced Indic cognates are mapped to the existing consonants resulting in renderings such as <span class="tamil">เฎชเฏเฎคเฏเฎคเฎชเฏเฎคเฎฟเฎธเฎคเฏเฎคเฏเฎตเฏเฎชเฏเฎฏ๊</span> /puttapotisattvepya:/. In publications that require fidelity to the original source, they are usually represented using superscript/subscript numerals e.g. <span class="tamil">เฎชเฏยณเฎคเฏยณเฎคโดเฎชเฏยณเฎคเฎฟโดเฎธเฎคเฏเฎคเฏเฎตเฏเฎชเฏโดเฎฏ๊</span> /buddhabodhisattvebhyaแธฅ/. While this is easy to typeset and provides accurate transliteration, the result is not visually pleasing and also hard to read. The other drawback is that many readers tend to simply ignore the diacritics and read the text as if it were normal Tamil.<br/>
