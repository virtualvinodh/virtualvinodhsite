    <p class="text-h5">Agastya: Extended Tamil font</p>
    <p class="text-body2">
Agastya is a Unicode fontset for the Extended Tamil script, an experimental attempt to use the full complimentary set of Grantha letters to fill in the gaps in the Tamil script (as compared to the pan-Indic system). </p>

<p class="text-h6">Extended Tamil Script</p>
<p>Modern Tamil only adopts /ja/, /ลa/, /แนฃa/, /sa/, /ha/ and the compound /ลrฤซ/ from the Grantha script into its character reportoire. The missing aspirated/voiced Indic cognates are usually represented using superscript/subscript numerals e.g. <span class="tamil">เฎชเฏยณเฎคเฏยณเฎคโดเฎชเฏยณเฎคเฎฟโดเฎธเฎคเฏเฎคเฏเฎตเฏเฎชเฏโดเฎฏ๊</span> /buddhabodhisattvebhyaแธฅ/. While this is easy to typeset and provides accurate transcription, the result is not visually pleasing (the text often resembling mathematical formulae) and also hard to read. The other drawback is that many readers tend to simply ignore the diacritics and read the text as it were normal Tamil. <br/> Extended Tamil attempts to fix this by importing Grantha characters and using them as if they were Tamil characters. Apart from improving the visual aesthetics of the text and its readability, the imported consonants create a one-to-one visual binding with the phonemes that the reader cannot easily disregard.</p>
