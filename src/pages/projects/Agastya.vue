<p class="text-h6"> Lopamudra </p>
<p>Sanskrit and Tamil have different orthographies. Sanskrit is an (almost) phonetic script while Tamil is (largely) phonemic. Particularly, Tamil unvoiced plosives have several allophones based on their context. Due to this, people often  apply Tamil reading rules to Sanskrit text and read it as it were Tamil. For instance, one might be tempted to read <span class="tamilextended">เดเดคเตเดฐเตโเดฎเดเตเดเดฆเตเดชเดพเดจเดฟ</span> as /sadurmakuแธadฤซbฤni/ using Tamil reading rules as opposed to /caturmakuแนญadฤซpฤni/ using Sanskrit rules. Therefore, there might be a need to explicitly indicate that these consonants should be pronounced using their original phonetic values. We have attemped to do this by slightly modifying the plosives, as in <span class="lopamudrasans">เดเดคเตเดฐเตโเดฎเดเตเดเดฆเตเดชเดพเดจเดฟ.</span></p>

