<p class="text-h6">Agastya</p>
<p> Agastya allows users to typeset such Extended Tamil text seamlessly. Since, Grantha and Tamil are closely related, extra care was taken to harmonize the additional Grantha characters with the existing Tamil characters, while at the same time, decreasing the confusability with similar-looking Tamil characters. Accordingly, we use Tamil-style descenders for the Grantha consonants as opposed to the flat-tail of traditional Grantha characters - <span class="granthano">๐ญ๐</span><span class="tamil">เฎค</span> vs <span class="tamilextended">เดญเตเดค</span>. Inspired by their earlier manuscript forms, the characters /da/, /kha/, /tha/ & /dha/ have been modified slightly from their normative printed forms to sufficiently differentiate from them other similar looking Tamil letters. <span class="tamilextended"><ruby>เด<rt>U</rt></ruby>&nbsp;<ruby>เดฆ<rt>DA</rt></ruby> | <ruby>เดต<rt>VA</rt></ruby>&nbsp;<ruby>เด<rt>KHA</rt></ruby> | <ruby>เดฏ<rt>YA</rt></ruby>&nbsp;<ruby>เดฅ<rt>THA</rt></ruby>&nbsp;<ruby>เดง<rt>DHA</rt></ruby> </span> as opposed to <span class="idhmavahasans">เด เดฆ | เดต เด | เดฏ เดฅ เดง </span>. Also based on manuscripts, /แนญh/ now has an additional inner dot to disambiguate it from the Anusvara - <span class="tamilextended">เดเด</span> /kaแนญha/ vs <span class="tamilextended">เดเด</span> /kaแน/.

<p>Agastya is, therefore, a perfect font to render any Indic text, where pronunciation is paramount and one needs a visually pleasing yet unambiguous way to represent the sounds of the source text.</p>
