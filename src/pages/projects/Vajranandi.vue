    <p class="text-h5">Design Motivation</p>
    <p class="text-body2">
    Initially, I wanted to redesign the glyphs so that they look similar to Devanagari. As I was trying to tweak them, it occured to me it is easier to evolve them into having a shared serif a.k.a the horizontal line of Devanagari rather that doing it in a ham-fisted way. We can readily draw from the fact the top horizontal strokes in the Indic scripts share the same origin.
    </p>
    <q-img src="../../assets/serif-common.png" width="300px" class="q-ma-sm"/>
    <br><i>(From Bharatiya Prachina Lipimala by Ojha. I have highlighted the development of the serif in red)</i><br/><br/>
    <p>In the above image, we can clearly see how an inconsequential serif eventually became part of the letter /ka/ in Devanagari, Kannada, Grantha and Tamil. In scripts like Devanagari рдХрдХ and Bengali ржХржХ, this serif eventually became a full fledged horizontal line that connects with adjacent letters, forming the distinctive horizontal line above. In scripts like Siddham <span class="siddham">ЁСЦОЁСЦО</span> and Sharada <span class="sharada">ЁСЖСЁСЖС</span>, they are not joined and remain distinct. In scripts like Tamil роХроХ, Telugu р░Хр░Х and Kannada р▓Хр▓Х the serif became an integral part of the glyph. Scripts like Gujarati ркХркХ and Kaithi <span class="kaithi">ЁСВНЁСВН</span> dropped the serif altogether.</p>
    <p class="text-body2">
    This raises the following interesting questions.
    <ul>
    <li>How would the Tamil script appear had it evolved a shared serif like Devanagari/Bengali?</li>
    <li>How would the letters morph to connect with each other?</li>
    <li>How could this be done while retaining a natural ductus (writing motion of the characters)?</li>
    </ul>
    </p>
    <p class="text-h5">Design Process</p>
    <p>Since Tamil already has a serif that is long and horizontal (albeit self-contained within each letter), it is straight-forward to extend them in both directions as in Devanagari. However, this will now result in strokes that are superfluous (and inhibit the ductus), as they appeared as auxillary strokes alongside the serif. Such strokes were removed, giving us: роХ роЩ роЪ род рои ро░ роХрпНро╖ :: <span class="vajranandi">роХ роЩ роЪ род рои ро░ ро▒ роХрпНро╖</span>. e.g. роироирпНродро┐ :: <span class="vajranandi">роироирпНродро┐</span>. But, then, there are characters that start with a loop (and in many cases they do not have a serif of any sort). How do we deal with them? In this case, we draw our inspiration from Bengali. It is common there for the serif to curve and attach to the character in order to the maintain the connectivity e.g. ржХрж╢ ржХрзЗ. Notice, how the serif of /ka/ curves gradually to connect with the letter /┼Ыa/ and the vowel sign /e/. Similarly, the characters with loops were modified to directly flow to/from the extended serif, giving us: роЮ рог ро╡ ро▓ ро│ рой ро╖ ро╕ ро╣ роЬ роО роП роР роТ роУ роФ роХрпЖ роХрпЗ роХрпИ to <span class="vajranandi">роЮ рог ро╡ ро▓ ро│ рой ро╖ ро╕ ро╣ роЬ роО роП роР роТ роУ роФ роХрпЖ роХрпЗ роХрпИ</span>. e.g. роЪрогроирпНродро┐ :: <span class="vajranandi">роЪрогроирпНродро┐</span>. For other letters, the serif was appropriately drafted onto the letters with slight modifications to the original shapes to maintain the overall aesthetics, giving us: роЯ рок роо роп ро╢ ро┤ роЙ роК роЕ роЖ роЗ роИ роХро╛ to <span class="vajranandi">роЯ рок роо роп ро╢ ро┤ роЙ роК роЕ роЖ роЗ роИ роХро╛</span>.
