    <p class="text-h5"> Supporting Superscript/Subscript Numerals </p>
    <p> Given that the purpose of the font is to evoke a Nagari-like feel, it is mostly likely be used to display Sanskrit text. It would make sense if it could support superscript/supscript numerals commonly used to write Sanskrit in Tamil. For superscript numerals, they are placed on the serif directly above the character similar to a vowel sign. <span class="vajranandi">роХ┬▓ роХ┬│ роХтБ┤</span>. If there is a combining vowel sign, it is placed above the vowel sign. <span class="vajranandi">роХро┐┬▓ роХро┐┬│ родрпАтБ┤</span> Any Vedic marks, if used, are placed above the superscript numerals. <span class="vajranandi" style="line-height:3em">роктБ┤с│Ъ рокро┐тБ┤с│Ъ</span>. However, the subscript numerals naturally find their found below the shared serif. <span class="vajranandi">роктВВ роктВГ роктВД рокро┐тВГ рокрпАтВГ роктВДс│Ъ рокро┐тВДс│Ъ</span>. Also, usually the subscript/superscript numerals are split from the base consonant with a spacing vowel sign, as in роХро╛┬│ роХрпЛтВГ. Vajranandi, rightfully places them next to the consonant. <span class="vajranandi">роХро╛┬│ роХрпЛтВГ</span>.</p>
