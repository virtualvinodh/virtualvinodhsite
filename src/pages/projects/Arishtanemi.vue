    <p class="text-h5">Arishtanemi: An Faux Hisotrical Tamil Font</p>
    <p class="text-body2">
        The Modern Tamil script is a culmination of 2000 years of evolution, with every serif, every retrace and every other accidental stroke constantly augmented to the characters by several generation of scribes. This clearly explains how a simple Brahmi character such as <span class="brahmi char">ëÄì</span> ended up evolving as <span class="tamil char">‡Æï</span>. The development of the Tamil script as outlined by Ojha in his book <i><a href="https://archive.org/details/the-paleography-of-india-bharatiya-prachin-lipimala-1919-rai-bahadur-pandit-gaur/page/n1/mode/2up">The Paleography of India</a></i> can be seen here.
    </p>
    <q-img src="../../assets/projects/tamil-development.png" width="400px" class="q-ma-md"/>
    <p class="text-body2">
        I had been wanting to create a tamil typeface that essentially de-noises the modern Tamil script by removing all the historical additions to the Brahmi characters over the years. The idea was to and make them look as close as possible to the Brahmi characters and give them a faux-historical flair. However, I also wanted the resulting set to be readable to the modern audience. Accordingly, I draw from various medieval Tamil scripts to retain the readability of the character set, where necessary.
    </p>
    <p>
        Consider, the Tamil character <span class="tamil char">‡Æ§‡ØÅ</span>. This might look complicted. But the underlying skeleton of the character is very simple <span class="tamilbrahmi char">ëÄ¢ëÄº</span>. The loop below the baseline is just a result of inexact retracing that got standardized. And the top line is just a relic of serif that was added to most Brahmi characters by scribes. If one attempts to remove all that, you actually end up something like <span class="arishtanemi char">‡Æ§‡ØÅ</span>. On the same note, the initial loop in <span class="tamil char">‡Æ≤</span> is basically an artefact of handwriting. Removing that would result in <span class="arishtanemi char">‡Æ≤</span>, making it look similar to <span class="tamilbrahmi char">ëÄÆ</span>.
        Of course, this simplification could not be performed 100%, else the result would be totally unreadable to the modern audience. So, there needed to be some balance between simplification and readability. For instance, the serif was partially retained over <span class="arishtanemi char">‡Æ®</span>. Without it, the character would have looked similar to <span class="tamil char">‡Æü‡ØÅ</span>.
    </p>
    <p>Some characters such as <span class="tamil char">‡Æ£</span> had to be reshaped based on medieval forms rather than Brahmi, as the current character has too many details and removing all of them would make it unrecognizable. In fact, if you attempt to write <span class="tamilbrahmi char">ëÄ°</span> with a single stroke by starting the character at the left arm and moving the stylus along the stem and retracing the bottom left and right legs and the stem again to reach the right arm, you'll end up something like this <span class="arishtanemi char">‡Æ£</span>. <span class="tamil char">‡Æ±</span> has been transfromed to <span class="arishtanemi char">‡Æ±</span> inspired by the Vatteluttu form <span class="vatteluttu char">‡Æ±</span>. Similarly, compare <span class="arishtanemi char">‡Æ¥</span> to the Vatteluttu form <span class="vatteluttu char">‡Æ¥</span>. The vowels signs were also inspired from the medieval forms as the Brahmi vowel signs are tightly bound to the base consonants. (As time progressed the vowel markers were separated and became independent glyphs). Again, for the sake of familiarity it made sense to imitate them rather than Tamil-Brahmi.</p>
