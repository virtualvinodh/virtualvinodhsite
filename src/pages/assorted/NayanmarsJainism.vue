<p>The śramanic traditions namely Buddhism & Jainism had a great following in Tamil Nadu until the early centuries of the Common Era. The early rivalry was chiefly between Buddhism and Jainism. However, the dominance of Buddhism gradually declined (due to various reasons that needs to be discussed seperately) and Jainism gained a upper hand in Tamil Nadu. Jainism dominated the religious scene until 7th-8th Century CE with its main rival being the Vedic Shaivaite religion. As a dominant religion of that time Jainism had a great influence on Tamil Shaivism.</p>

<p>In the Tamil Shaivaite world, 63 <i>nāyaṉmārs</i> are a set of 63 legendary saints who are widely venerated. The 63 Nayanmars supposedly lived in a time where the Shramanic Jaina religion was very popular. Traditionally the nayanmars are said to be one of the reason for the downfall of the Jaina Religion and popularizing the Vedic Shaivism among the public.</p>

<p>The Tamil epic <i>Periya Purāṇam</i> is a work which describes the legends of those 63 Tamil Shaivaite saints. The epic was written by Sekkizhar around 12th Century CE. The background story for the composition of this epic is quite interesting. The Chola king Kulōttuṅka was very fond of the Jaina Epic called <i>Cīvaka-Cintamani</i> (Sanskrit: <i>Jīvaka Cintāmaṇi</i>)".  It describes the various adventures of the Jaina king Jivaka and also discusses Jaina philosophy alongside narration.</p>

<p>Sekkizhar, a staunch Shaiviate, the chief minister in the Chola king's assembly was deepy affected by the king's interest in the heretic Jaina epic. So, he started composing a Tamil shaivaite Work which would surpass the Jaina epic and rekindle the Chola king's interest in Shaivaite works. He then started collecting various legends that were in circulation in his times and composed his work.</p>
<p>Now an important question arises. How did Sekkizhar come with this magical number 63?</p>

<p>The answer lies with Jainism, the religion which he wanted to oppose. In the Jain religion, there existed 63 pious Jainas called the <i>triśaṣṭi śalāka puruṣas</i>. Their lives and legends are detailed in the Sanskrit language epic called <i>Mahā Purāṇa</i>. Based on this Sanskrit work, the Tamil Jainas translated the work into Tamil as <i>śrī purāṇam"</i> using the Manipravala style (admixture of Sanskrit & Tamil) . The Shri Purana is widely venerated among the Tamil Jains (who are now reduced to a micro-minority) even upto this day. Interestingly, the Shri Purana is also known as "Periya Puranam".

<p>The 63 pious persons as described in Shri Purana are:</p>

<p>1. 24 Tirthankaras</p>

<p>2. 12 Chakravartins</p>

<p>3. 9 Vasudevas</p>

<p>4. 9 Prati-Vasudevas</p>

<p>5. 9 Baladevas</p>

<p>All adding upto 63.</p>

<q-img src="../../assets/assorted/nayanmars-2.jpg" width="200px" class="float-left q-ma-md"/>

<p>Probably, Sekkizhar wanted a Shaivaite version of Shri Purana which would be widely venerated among the Shaivas. Instead of the Jain saints, he replaced it with the 63 Shaivaite saints. At the time of Sekkizhar, even though Jainism had lost significant ground, it was still dominant in the intellectual circles. The Shaivaites possibly wanting to imitate the Jainas and created their own list of 63. Till this day, the Shaivaites are venerating the group of 63 persons who are modeled upon the Jaina Trishashti Salakapurushas. Little do they realize that this number was borrowed from their historical rivals, the Jains. Also Shaivaites had absorbed many of the attributes of Arhat Parameshvara and attributed them to Shiva. The Shranamic traditions had indeed a very huge impact on the Vedic Brahmanic religion and, hence, modern day Hinduism.</p>
