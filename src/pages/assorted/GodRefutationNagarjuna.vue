<p><b>Refutation of the view of God being the creator of the world and of the view of Vishnu being the sole creator of the whole world</b></p>
<p><b>Īśvarakartṛtva-nirākṛtiḥ-viṣṇoḥ–ekakartṛtva–nirākaraṇa</b></p>
<p><i>(Attributed to the Philosopher Nagarjuna)</i></p>
<p>[It is claimed by some that] there exists a God, who is the creator [of the world]. Let he be critically examined [by us also].</p>

<p>The creator is one who creates. One who performs a [certain] action is called the creator [in relation to that action].</p>

<p>In this regard, we argue [as follows].</p>

<p>He can create something which we know as existent (siddha) or which we know to be non-existent (asiddha).</p>

<p>First, it may be remarked here that He cannot be the creator of something which we know as existent, because the concept of the creator cannot be applicable to such an object. For example, we know that man exists. Creating him further cannot be an act of creation; because his existence is already established [i.e. before this alleged creation by God].But it may be argued that God creates something which is [already] known to us as non-existent. [To this we answer as follows]; Let it be that He also Creates those objects: oil [crushed] out of sand, which is known to us as nonexistent; wool on a tortoise, which is known to us as non-existent. [Let God create all these also]. But He does not have the power of creating these objects. Why? Because these are known to us as non-existent,And He [God] is also similar [i.e. God also is non-existent]. </p>

<p>Now [it may be argued that] He makes non-existent existent [i.e., God creates an object which was previously non existent but which becomes existent as a result of this divine creation]. But this is also impossible, because of mutual contradiction [i.e. because the existent and the non-existent are mutually exclusive concepts]. Something which exists is existent [andin no case whatsoever being existent it can become non-existent]. And that which does not exist is never other than the non-existent. [Hence, in no case, being non-existent can it become existent]. Thus, between these two Concepts, there is inevitable mutual contradiction,—like that between light and darkness, between life and death. In fact, where there is light there is no darkness and where there is darkness in no case can there be light. Who is alive, is alive [and not dead] and who is dead, is dead [and not alive]. Therefore, since there is no unity between the existent and the non-existent, in no case can God be the creator [of the existent out of the non-existent].Besides, there will be further objections. Does the creator, who creates something external to Him, create it being Himself born or unborn? He cannot create something external to Him being Himself unborn.</p>

<p>Why? Because He is Himself something unborn, like ‘the son of a barren woman’, who being unborn, cannot perform any action like the digging of the earth. God also is in such a position. [Being Himself unborn, He cannot create things external to Himself].[Now, we shall analyse the other case]. He creates the external things after being Himself born. But wherefrom is He born ? Is He born out of Himself or from something else or from both [i.e., both from Himself and something external to Himself]?As regards the first of these alternatives, it needs to be observed that He could not have been born out of Himself, since one’s own actions cannot relate to one’s own self. The blade of a sword, howsoever sharp it may be, cannot cut itself. Even the most expert dancer, howsoever skilful he may be, cannot dance standing on his own shoulders. Besides, it is never observed that one and the same object is the produced (janya) and the producer (janaka). A person, who. is the father is himself also the son — such an assertion isquite unknown in common discourse.</p>

<p>Now let us assume that God originates from something else. But this cannot be assumed, because in the absence of God there will also be the nonexistence of everything else.But [may be] He originates [from something else not direcpy but] through a number of successive factors (päramparya). However, in that case we wouldhave a number of infinite factors depending upon something else because God, according to His nature, has no origin (anädi). But if something really exists and is without beginning, we cannot assume that it would have a continuation. [Literally: “Where there is absence of beginning, this very absence of beginning is the refutation of the end”]. When there isno seed, there results also the absence of the sprout, trunk, branches, leaves, flowers, fruits, etc.</p><p>Why? Because of the absence of the seed itself. [Exactly, in the,same manner, if God does not have a beginning, He must also be Himself non- existent].Nor can [He be born] out of both [His own self and something else]. Because such an assumption would suffer from both froms of fallacies [i.e. both Ihe fallacies involved in the first two alternatives].</p>

<p>Therefore, we cannot conceive the creator of the world as something existing.</p>

