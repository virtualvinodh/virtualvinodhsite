    <p class="text-h5">Shiva in Buddhism</p>
        <p><small><i>Composed: Mar, 2011. Updated: Sep, 2020</i></small></p>
<q-img src="../../assets/assorted/buddha-2.jpg" width="200px" class="float-left q-ma-md"/>

<p>During the early days of formation, Buddhism didn’t have many deities to deal with. The predominant deities of those times were Indra and Brahma. In the tripitaka, all these deties are shown completely subservient to the Buddha. They are potrayed as waiting and attending upon the Buddha and receiving many teachings from the Buddha himself. At one instance, it is even said the Maha Brahma himself had advised a Brahmin who had reached his realm to clear his doubts with the Buddha instead of him.</p>
<p>As Buddhism evolved over times, it had to face the new puranic Brahmanism with the influence of  Vishnu, Shiva and other deities rising over time. The other Vedic deities  were now often reduced to a tutelary status. Puranic Brahmanism was highly syncretic assimilating everything that came in its way. Even Buddha himself was assimilated into its Pantheon as one of the avatars of Vishnu. At the same time, Buddhism had also attempted in assimilation of the Brahmanical deities into its fold. However, this part is not that much widely known.</p>

<p>Buddhism in its Theravada version was more orthodox and didn’t officially expand beyond the already assimilated Indra, Brahma and other early deities. Though Sri Lankan Theravada did assmilate Vishnu as a Kshetra Pāla, Protector of the Land but that was outside the Theravada Canon. In comparison, Mahayana Buddhism was highly assimilative in nature. The framework of Mahayana easily enabled the direct import of deities. When Mahayana was faced with Shaivism and Vaishnavism, it had also tried its part of trying to assimilate the then popular Shiva and Vishnu.</p>

<p>More specifically, we’ll deal only the assimilation of Shiva as a deity into Buddhism. Shiva was initially considered as an emanation of Avalokiteshvara. It was also believed that Shiva himself will attain Buddhatva (i.e Buddhahood) in the future as Bhasmeshvara Buddha.  We’ll see some Buddhist Sutras that directly reference to Shiva as a deity. (There are also Buddhist tantras that deal with Shiva but that's for another day)</p>

<p class="text-h6">Lotus Sutra</p>
<q-img src="../../assets/assorted/buddha-1.jpeg" width="150px" class="float-left q-ma-md"/>
<p>Lotus Sutra i.e the <i>Saddharma Puṇḍarīka Sūtra</i> is one of the earliest Mahayana sutra that speaks of Shiva as an emanation of Avalokiteshvara. The 24th chapter of the utra, “The Chapter on the Universal Door [of Avalokiteshvara]” (<i>Samanta-Mukha-Parivarta</i>) deals with description of the various emanation of Avalokiteshvara in the Sahā Lokadhātu, our current universe.</p>

<p>In this chapter, Bodhisattva Akshayamati proceeds to ask the Buddha on how Avalokiteshvara frequents this world, the various methods he teaches the dharma and also the skillful means (<i>upāyakauśalya</i>) adopted by him to teach the dharma to all sentient beings.</p>

<p class="blockquote"><i>bodhisattva-avalokitesvaraatha khalvakṣayamatirbodhisattvo mahāsattvo bhagavantametadavocat-kathaṁ bhagavan avalokiteśvaro bodhisattvo mahāsattvo’syāṁ sahāyāṁ lokadhātau pravicarati? kathaṁ sattvānāṁ dharmaṁ deśayati? kīdṛśaścāvalokiteśvarasya bodhisattvasya mahāsattvasyopāyakauśalyaviṣayaḥ?</i></p>

<p class="blockquote">Again the bodhisattva mahāsattva Akshayamati said to the lord: How, O lord, how is it that the bodhisattva bahāsattva Avalokitesvara frequents this Saha-world? And how does he preach the dharma? And what is the range of the skilfulness of the bodhisattva mahāsattva Avalokitesvara?</p>

<p>The Buddha replies as follows:</p>

<p class="blockquote"><i>evamukte bhagavānakṣayamatiṁ bodhisattvaṁ mahāsattvametadavocat-santi kulaputra lokadhātavaḥ yeṣvavalokiteśvaro bodhisattvo mahāsattvo buddharūpeṇa sattvānāṁ dharmaṁ deśayati […] keṣāṁcit pratyekabuddharūpeṇa avalokiteśvaro bodhisattvo mahāsattvaḥ sattvānāṁ dharmaṁ deśayati| […] keṣāṁcid brahmarūpeṇāvalokiteśvaro bodhisattvo mahāsattvaḥ sattvānāṁ dharmaṁ deśayati […] maheśvaravaineyānāṁ sattvānāṁ maheśvararūpeṇa dharmaṁ deśayati […]</i></p>

<p class="blockquote">So asked, the lord replied to the bodhisattva mahāsattva Akshayamati: There are [some] worlds, young man of good family, [where] the bodhisattva mahāsattva Avalokitesvara preaches the dharma to creatures in the form of a Buddha […] to some [beings] he preaches the Dharma in the form of a pratyekabuddha […] to some [beings] he preaches the dharma in the shape of a brahma […] [to those] who are to be converted by Mahesvara, he preaches assuming the form of Mahesvara. […]</p>

<p>The Buddha describes that Avalokiteshvara by his skillful means takes numerous forms like that of a Pratyekabuddha, Shravaka, Brahma, Yaksha, Gandharva including that of Shiva to teach the dharma to the sentient beings. It must be noted that Shiva is not specifically discussed here, he is just included as a passing reference among all other emanations of Avalokiteshvara. It can be considered that at this time the Buddhists had begun to consider Shiva as an emanation of Avalokiteshvara.</p>

<p>As Shiva was considered as a emanation of Avalokiteshvara, many of Shiva’s features were also attributed to Avalokiteshvara, again an attempt to subsume Shiva under Buddhism. In fact, not specifically Shiva but the attributes of the dual deity Hari-Hara or Shankara-Narayana seems to have been overlaid on Avalokiteshvara. This requires a detailed study on its own. More should be written about this at some point in the future.</p>

<p class="text-h6">Karandavyuha Sutra</p>
<q-img src="../../assets/assorted/avalokitesvara-1.jpg" width="150px" class="float-left q-ma-md"/>

<p>Karandavyuha is one of the major Mahayana sutras that is devoted completely to bodhisattva Avalokiteshvara. It attempts at  the assimilation of the all Brahmanical deities at one go, by establishing Avalokiteshvara as the progenitor of those deities.</p>

<p>The Buddha is described as being resident at Sharavasti in the Garden of Anathapindaka along with a host of bodhidhattvas and bhikshus. The deities of the heavenly realms, headed by Vishnu and Shiva, are also shown to attend the Buddha’s sermon. Shiva himself is potrayed as attending the Buddha’s sermon. In the fourth chapter of the sutra on the creation of moon and others (<i>candrādyutpattirnāma caturthaṁ prakaraṇam</i>), the Buddha explains how Avalokiteshvara assumes the form of Srishtikarta Lokeshvara to emanate all the Brahmanical deities from his body</p>

<p>Specifically, Buddha says that Shiva was born from Avalokiteshvara’s forehead: <i>lalāṭānmaheśvaraḥ</i> (from forehead Maheshvara).</p>

<p>After emanation of Shiva from his body, Avalokiteshvara tells him that during the kaliyuga he will appear in a degenerate realm and will be called the primordial god (<i>ādideva</i>), the creator and author of the world. The beings in this realm at that time will be deprived of the path to awakening and the talk among those foolish common people will be: “It is said that space is his linga and the earth his pedestal. He is the ground of all and is called linga because all beings dissolve into him”. The Shaivaite doctrine is directly rebuked here. The people worshipping Shiva as the creator deity are criticized as “foolish common people” (<i>pṛthagjana</i>).</p>

<p class="text-h6">Buddha Bhasmeshvara</p>
<q-img src="../../assets/assorted/shiva-a.jpg" width="150px" class="float-left q-ma-md"/>
<p>Moving further to the narrative end of the sutra, Shiva appears before the Buddha, prostrates before him and asks for a vyakarana (prediction of buddhahood). He is directed by the Buddha to Avalokiteshvara. Maheshvara praises Avalokiteshvara and now requests Vyakarana from him instead. On his request Avalokiteshvara bestows the below prophecy.</p>

<p class="blockquote"><i>bhaviṣyasi tvaṁ kulaputra vivṛtāyāṁ lokadhātau bhasmeśvaro nāma tathāgato’rhan samyaksaṁbuddho vidyācaraṇasaṁpannaḥ sugato lokavidanuttaraḥ puruṣadamyasārathiḥ śāstā devānāṁ ca manuṣyāṇāṁ ca buddho bhagavān</i></p>

<p class="blockquote">Kulaputra, you will in the Vivrita-world become a Tathagata named Bhasmeshvara, an Arhat, one who is perfect in knowledge and conduct, a Sugata, a knower of the world, an unsurpassed-one, a tamer of men, a teacher of the gods & men, a Buddha and a lord</p>

<p>(See Shiva's praise of Avalokiteshvara <a href="writings/mantras/karandavyuha-stotras">here</a>)</p>

<p class="">The tantra text <i>Sarva-Tathagata-Tattva-Sangraha</i> also contains information about Shiva’s conversion as the future Bhasmeshvara Buddha but with a different narrative. We’ll deal that along with the other tantra references to Shiva later.</p>

<p>Though the shaivaite doctrine is criticized at the beginning, ultimately at the end, shiva is accepted into Mahayana Buddhism as a future completely enlightened Buddha.</p>
