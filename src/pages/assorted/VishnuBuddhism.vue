    <p class="text-h5">Vishnu in Buddhism</p>
        <p><small><i>Composed: Aug, 2012. Updated: Sep, 2020</i></small></p>
<p>It’s a well-known fact that the Buddha was at a later phase was absorbed (begrudgingly if I may add) into the Hindu pantheon as an avatar of Vishnu. This is frequently invoked by the modern Hindus to encompass Buddhism as a part of Hinduism. What is not well known is the status of Vishnu as a deity within Buddhism per se. In a <a href="/writings/assorted/shiva-buddhism">previous article</a>, we saw the status of Shiva in Buddhism, and now we analyze in detail the status of Vishnu vis-à-vis Buddhism.</p>

<p>Compared to Shiva, who has relatively more references in the Mahayana Buddhist sutras and the numerous Vajrayana tantras (where he appears as Rudra/Maheshvara to be always subdued by the wrathful Buddhist deities at the end), references to Vishnu are comparatively less. This perhaps may be due to the fact that the later Buddhists were in a position to directly interact more with the Shaivaites than the Vaishnaivites.</p>

<p class="text-h6">Theravada Buddhism</p>
<p>As already referred in the “Shiva in Buddhism” article, Theravada Buddhism is quite orthodox in admitting deities into its fold. Rather than integrating the deities directly into the religion, it tends to keep the deities in the periphery as local deities to be worshipped for favor or as Dharmapālas (Dharma Protectors).</p>

<q-img src="../../assets/assorted/visnu-1.jpg" width="180px" class="float-left q-ma-md"/>
<p>In Sri Lankan Theravada, Vishnu is worshipped as “Upulvan” (Pali. <i>Uppala-Vaṇṇa</i>). The equivalent Sanskrit title is “Utpala-Varṇa” (Blue-Lotus-Colored). Vishnu is frequently depicted as blue colored (blue being a euphemism for the original black color of the deity). Vishnu as Upulvan is the <i>Kshetra-Pāla</i> (protector of the land) of Sri Lanka. Buddha before his parinirvana is believed to have recognized Sri Lanka as the place where his shāsana (law) will flourish in the future. Therefore, he orders Indra to protect the island. Indra then entrusts the protection of the island to Vishnu. (Incidentally, Vishnu is actually subordinate to Indra in the Vedas. This legend appears to preserve the early status of Vishnu as a sub-ordinate deity).</p>

<p>Here Vishnu is seen by the lay Sri Lankan Buddhists as a “bodhisattva” on his way to Buddhahood and a firm protecter of the “buddha shāsana”.</p>

<p>The incident is established in Dipavamsa (circa 3th century CE) and the later Mahavamsa (circa 5th century CE). It has been also posited that Upulvan is a local deity, who later merged with Vishnu. But most probably it is  correct to identify “Upulvan” with an early form of Vishnu (before he became a supreme deity in the later puranic Hinduism.)</p>

<p>Though Thailand is currently a Theravada nation, Mahayana Buddhism and Hinduism dominated its religious lanscape for a quite a period of time. These influences are still deep and very visible. Because of the multi-cultural past, Thailand has no issues in venerating Hindu deities per se. In fact, Vishnu temples can be still found scattered around in Thailand. There is no specific status of Vishnu through the context of Thai Buddhism. His worship is derived by the nation’s Hindu past and is best analyzed as the remnant of Hinduism in Thailand.</p>

<p class="text-h6">Mahayana Buddhism</p>
<q-img src="../../assets/assorted/vishnh-2.jpg" width="180px" class="float-left q-ma-md"/>
<p>The interactions between later day Hinduism and Buddhism are more vividly presented in the context of Mahayana Buddhism. The deities were absorbed to the core of the religion along with their original myths and beliefs reinterpreted in the context of Buddhism. As a result, Mahayana tends to present the deities in its own unique context but retaining most of the original characterestics.</p>

<p>In the Karandavyuha Sutra, he is called as “nārāyaṇa pañcamahāsamudranamaskṛta”, Narayana worshipped by the five oceans. But this seems to be a hypercorrection. In a Pāṭha-Bheda (alternate reading) of the Shurangama Dharani, he is referred as “nārāyaṇa pañcamahāmudrānamaskṛta“, Narayana worshipped by the five great mudras. The latter sounds more sensible. It probably refers to the five weapons usually associated with Vishnu.</p>

<p class="text-h7">Vishnu as Emanation of Avalokitesvara</p>
<p>As with <a href="/writings/assorted/shiva-buddhism">Shiva</a>, Vishnu is also seen as an emanation of Avalokitesvara. In this context, one must note the multiple nature of devas in Mahayana. Though the deities are generally recognized as distinct entities in their own right, it must be understood that Buddhas & bodhisattvas may also “assume the form” of these deities to instruct dharma as a part of Upāya-Kaushalya (skillful means). In other cases, the deities themselves may be presented as emanations of Buddhas & bodhisattvas.</p>

<p>In the Karandavyuha sutra, it is said that from Avalokitesvara’s heart Narayana was emanated, “hṛdayānnārāyaṇaḥ”, for the benefit of sentient beings.</p>

<p>[See: <a href="/writings/assorted/srishtikarta-lokeshvara">Srishtikarta Lokesvara</a> for the detailed account].</p>

<p>In the initial part of the sutra, Vishnu is included in the assembly of Devas to whom Buddha preached the sutra. In the eighth chapter of the sutra (<i>vaineyadharmopadeśa</i>), the Buddha expounds :</p>

<p class="blockquote"><em>yena yena rupeṇa vaineyāḥ sattvāḥ, tena tena rupeṇa dharmaṁ deśayati [..] nārāyaṇavaineyānāṁ sattvānāṁ nārāyaṇarūpeṇa dharmaṁ deśayati</em></p>

<p class="blockquote">In whatever forms the beings are to be converted, [Avalokitesvara] instructs dharma in those forms [...] [he] instructs dharma in the form of Narayana for the beings who are to be converted by Narayana </p>

<p>It is interesting to note that the the Lotus sutra doesn’t include Vishnu in the list of forms assumed by Avalokiteshvara. In fact, the list presented in the Karandavyuha is an expanded version of the list present in the Lotus sutra. This probably reflects the growing influence of Avalokitesvara, as more and more deities became associated with him.</p>

<p>(As a side note, the sutra also presents a Buddhist version of <a href="/writings/mantras/karandavyuha-stotras">Vamana Avatara</a> along with Bali)</p>

<p>The <i>ratnamala stotra</i> praises bodhisattva Avalokitesvara as follows:</p>

<p class="blockquote"><em>vaineyavaiṣṇavajanapratibodhanāya<br/>
rājīvapāṇihṛdayāt pratiniḥsṛto’sau<br/>
nārāyaṇo’pi bhuvaneśvara eva tasmāt<br/>
puṁsāṁ tvameva paramottama eva nānyaḥ</em></p>

<p class="blockquote">For the benefit of instructing the Vaishnavites who are to be converted [to the dharma]<br/>
He was put forth from the heart of [the] holder of lotus (Avalokitesvara)<br/>
[He is] Narayana indeed, [who is] the lord of the world too<br/>
Therefore (Oh! Avalokiteshvara) you are indeed the greatest of men too [and] no one else

<p>The last phrase “puṁsāṁ paramottama” directly refers to Vishnu. Vishnu is frequently referred to as “Purushottama” meaning “greatest of men”. The author implies that since Avalokitesvara is the progenitor of Vishnu, he is indeed the worthy of the epithet not Vishnu.</p>

<p>On similar lines, the <i>lokanatha stotra</i> has the following.</p>

<p class="blockquote"><em>brahmā tvameva hi sa viprakulaprasiddho<br/>
viṣṇuśca vaiṣṇavamate varadharmaketuḥ</em></p>

<p class="blockquote">You are indeed Brahma, he [who is] famous among the Brahmins<br/>
[You are also] Vishnu in the Vaishnava religion, the leader of noble dharma</p>

<p class="text-h6">Vishnu as Sub-Ordinate Deity</p>
<q-img src="../../assets/assorted/buddha-4.jpg" width="180px" class="float-left q-ma-md"/>
<p>There is an interesting dharani sutra with the title “nārāyaṇaparipṛcchā āryamahāmāyāvijayavāhinī nāma dhāraṇī”, the āryamahāmāyāvijayavāhinī named dharani requested by Narayana”. As explicitly indicated by the title, in the sutra Narayana is seen as requesting a dharani from the Buddha. The Buddha is residing at the city of Kubera expounding the Dharma named “Dharma-Aloka-Mukha” (The Bright Faced Dharma). Narayana appears there after being defeated by the Asuras. He then circumambulates the Buddha and pays homage to him by placing his head at his feet and requests him to preach the Dharani that grants victory in war.</p>

<p class="blockquote"><em>tadevaṁ deśayatu bhagavān sarvajñaḥ sarvadarśī sarvasattvānukampakastaṁ dharmaparyāyaṁ yamete devanāgayakṣarākṣasādayo manuṣyā vā dhārayamāṇāḥ saṁgrāme mahāśūlapātebhyo vā sarvopadravebhyo vā sarvavitarkavicārebhyo vā vijayino bhaviṣyanti</em></p>

<p class="blockquote">Therefore instruct, Oh lord, knower-of-all, observer-of-all and sympathizer-of-all beings, that dharma-paryaya (dharma-teaching) through which the devas, nagas, yakshas, rakshasas or men on bearing [it] will become victorious at war from the attack of great-tridents, all calamities and all-doubtful-thoughts.</p>

<p>On his request, the Buddha expounds the dharani.</p>

<p>(See the complete dharani <a href="">here</a>. The dharani verses commence with “tadyathā namo’stvadhvānugatapratiṣṭhitebhyaḥ” and conclude with “phaṭ phaṭ svāhā“)</p>

<p>After hearing the dharmopadesha (dharma-instruction),</p>

<p class="blockquote"><em>bodhisattvasaṁvarīyo nārāyanaḥ aho āścaryamiti kṛtvā śaṅkhacakragadāpuṣpamālyayuktaḥ utthāyāsanāt bhagavantaṁ triḥpradakṣiṇīkṛtya praṇamya prahasitavadano bhūtvā bhagavantaṁ gāthayā stauti sma|<br/>
<br/>
aho hyasuradevānāṁ lokānāṁ jyeṣṭhaṁ śreṣṭho hyanuttarīkaḥ|<br/>
śivaḥ śānto’thāgrāhya lokātīto namo’stu te||<br/>
abhāvaḥ sarvadharmāṇāṁ bhūtadharmaprakāśakaḥ|<br/>
dharmādharmavimuktaustau dharma satya namo’stu te||</em></p>

<p class="blockquote">Surrounded by the bodhisattvas, Narayana after having said “Oh! amazing”, rose up from the seat with [his] conch, discus, mace and flower garland and circumambulated the lord three times. [He then] bowed down [to him] with a smiled-face and praised the lord through a verse (gātha) .<br/>
<br/>
Behold! The foremost, best [and the] unsurpassed of the asuras and devas of the world<br/>
The auspicious, peaceful, un-conceived and the one beyond the world, homage to him<br/>
The illuminator of the past dharma of the non-existance [of] all-dharmas<br/>
[The] dharma-truth  liberated [from] dharma and ddharma, homage to him<br/></p>

<p>Then Narayana also utters the phrase “tvaṁ mama vibhuḥ" (you are my Lord) and then disappears. Alternatively, Vibhu also means all-pervading (Vishnu also means the same). Again, it seems like Vishnu is implying that only the Buddha deserves that title.</p>

<p><a href="/writings/assorted/hari-vahana-lokeshvara">Hari-Hari-Hari-Vahana Lokeshvara</a> is one of the 108 forms of Avalokiteshvara. In this specific form of Avalokitesvara, Vishnu serves as a vāhana (vehicle) for Avalokitesvara.</p>

<p>To summarize the story: The naga Takshaka was being hunted by Garuda and as a result both of them were locked in a battle. As Takshaka gained the upper hand, Garuda invoked Vishnu for protection. Vishnu immediately appeared and was about to use his discuss at Takshaka. On seeing this, Takshaka invoked Avalokitesvara instead. When Avalokitesvara appeared in a sihma vāhana (lion vehicle), Vishnu instantly threw away his chakra and paid homage to him. He then worshipped Avalokitesvara and offered himself (on Garuda) as a vāhana to him and Takshaka offered himself as a vāhana of the Lion. Thus, the composite image of Avalokitesvara-on-Vishnu-on-Garuda-on-Lion-on-Naga Takshaka was formed.</p>

<p>All the three, Garuda, Vishnu, and the lion, have the epithet of “hari”. Since these three haris serve as the vāhana (vehicle) of Avalokiteshvara, the composite image came to be known as Hari Hari Hari Vahana Lokeshvara.</p>

<p class="text-h6">Vajrayana Buddhism</p>
<p>As much as my limited knowledge of Buddhist Tantras is concerned,  there aren't any specific details of a “Buddhist Tantric” Vishnu. Vishnu appears as a parivāra deva (secondary deity) in several mandalas. Nishpannayogāvali, a Tantric text which describes various mandalas and the deities therein records the iconography of Vishnu as follows:</p>

<p class="blockquote"><em>garuḍe viṣṇuś-caturbhujaḥ cakraśaṅkhabhṛtsavyavāmābhyāṁ mūrdhni kṛtāñjalir-gadāśārṅgadharaḥ</em></p>

<p class="blockquote">On a Garuda there is Vishnu with four arms. With the two principal hands carrying the cakra and the shankha, he displays the anjali [mudra] on his head. With the two others he holds the gada and the bow.</p>

<p class="text-h6">Conclusion</p>
<p>The Buddha eventually being assimilated as an avatar of Vishnu is posited as one of the reasons for Buddhism’s decline. That appears to be too strong a statement. If that had been the case, given that Vishnu was assimilated into Buddhism, Hinduism should've declined as well. Syncretism was too common and rampant across the Indic religions and the assimilation happened in both the directions to have been the main reason for Buddhism's in India. In any case, Vishnu is potrayed as a venerable deity within the context of Buddhism albeit sub-ordinate to the Buddhas and bodhisattvas. To invoke the verses of the surangama dharani,</p>

<p class="blockquote"><em>namo nārāyaṇāya lakṣmī sahāyāya pañca-mahā-mudrā-namaskṛtāya</em></p>
<p class="blockquote">Homage to [the] Vishnu, who is accompanied by Lakshmi, and is worshipped by the five great mudras</p>

