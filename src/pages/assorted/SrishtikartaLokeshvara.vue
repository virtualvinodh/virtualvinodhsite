Sṛṣṭikartā Lokeśvara, one of the 108 forms of Avalokiteshvara, is perhaps the most interesting form of Avalokiteshvara, apart from Hari <a href="/writings/assorted/hari-vahana-lokeshvara">Hari Hari Vahana Lokeshvara</a>. Here Avalokiteshvara assumes the form of srishtikarta (i.e creator) and emanates, for the benefit of sentient beings, all the Vedic gods from his body. (As a side note, according to Lokesh Chandra, Bhattacharya misread sṛṣṭikartā as Sṛṣṭikāntā Lokeshvara in his Book.)</p>

<p>The depiction of Srishtikarta Lokeshvara is derived from the karandavyuha sutra, where Buddha Shakyamuni descibes to the assembly they way Avalokiteshvara emanated various Vedic gods . Here Avalokiteshvara assumes the form similar to that of the Vedic Purush abd produces all the deities from his body. Thus, he takes on the form of the creator i.e srishtikarta.</p>

<p>Bodhisattva Sarvanīvaraṇaviṣkambhī asks how did Buddha Shakyamuni come to know about Avalokishvara’s [such] manifestation of qualities (<i>guṇodbhāvanā</i>). The Buddha replies that the qualities of the Avalokiteshvara was heard by him from Buddha Vipashyin and also again from Buddha Sikhin, when he was a Bodhisattva named <i>sarvanīvaraṇaviṣkambhin dānaśūra</i>.</p>

<p class="blockquote"><i>cakṣuṣoścandrādityāvutpannau, lalāṭānmaheśvaraḥ, skandhebhyo brahmādayaḥ, hṛdayānnārāyaṇaḥ, daṁṣṭrābhyāṁ sarasvatī, mukhato vāyavo jātāḥ, dharaṇī pādābhyām, varuṇaścodarāt | […]</i></p>

<p class="blockquote">From [his] eye’s the [gods of] Sun and Moon, from  [the] forehead Shiva, from [his] shoulders Brahma, from [the] heart Vishnu, from [his] teeth Sarasvati, from [the] mouth Vayu (the god of wind), from [his] two feet the goddess of earth, Varuna  (the god of rain) from [the] stomach. […]</p>

<p>The above creation story is also repeated in the guna-karandya vyuha sutra. After Adi-Buddha Prabhasvara emanates Avalokiteshvara, Avalokiteshvara in turn emanates the Vedic gods from his body in the same fashion as described in the karandavyuha sutra.</p>
<q-img src="../../assets/assorted/srishtikarta-2.jpg" width="180px" class="float-left q-ma-md"/>

<p>Bhattacharya explains the iconography of this Avalokiteshvara as follows:</p>

<p class="blockquote">He is one-faced and two armed and stands on a lotus. He displays the Varada pose with his right hand, while his empty left hand rests near the navel. A large number of four armed gods issue from various parts of his body, while Amitabha appears over his head</p>

<p>Thus, the Buddhist bodhisattva Avalokiteshvara is in principle the source of the major Hindu deities. It is interesting to see that even though some Hindu sources identify Buddha as an subversive Avatar of Vishnu, Karandavyuha considers Vishnu himself as having emenated from the heart of bodhisattva Avalokiteshvara. Also, in the list of deities that are present in the assembly, both Shiva and Vishnu are observed to head the entire assembly of devas.</p>

<p>The <a href="/writings/mantras/karandavyuha-stotras">Karandyavyuha Sutra</a> describes a great deal of interaction that has supposedly taken place in between the Buddhist and Hindu figures.</p>
