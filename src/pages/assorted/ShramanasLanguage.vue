    <p class="text-h5">The Original Language of the Shramanas</p>
        <p><small><i>Composed: Aug, 2010. Updated: Sep, 2020</i></small></p>
<p>The Brahmanists have always regarded Sanskrit, as the mother of all languages.  As a matter of fact, this will be echoed in many modern Hindu adherants from Modern India. Latest to join the bandwagon, is the Tamil nationalists, who claim Tamil to be the progenitor of all languages. No Indian Language, or for that matter any other language, can truly claim as being the original proto-Language of the world. The existence of such a hypothetical proto-world language is itself contested. I prefer to view languages as originally emerging independant from each other, later influencing each other as different cultures spread around the globe.</p>
<p>
It is not surprising for the Brahmanists to claim the Vedic language as the mother of all languages. For according to Vaidikas, Vedas are apauruṣeya (vot [authored] by men). It was heard by the rishis, who were just instruments for writing down the cosmic sounds that they heard.  They are also considered to be anādi (without begninning). So for the everlasting and supreme Vedas to be supramundane, the language in which it is composed of must also deemed to be supramundane and perfect. It was language of the gods, after all.</p>
<p>
Now, coming to the Shramanic religions namely Buddhism and Jainism. They started out with the vernacular prakrita languages. They were more inclined to use the language of the masses to spread their teachings. In fact, many of the initial literature in the Indian regional languages were produced by the Shramanas. At one instance, the Buddha is said to have explicitly forbidden his teachings to be versified in Vedic language (Chandas) and only to be taught in the language of the masses. But as irony befalls, the Shramanas themselves fell into the same trap as their Vedic counterparts. They began considering their own version of the liturgical Prakrit language as the original source .</p>
<p>
One interesting thing to note is that the Shramanas considered Prakrit as the source of Sanskrit, not vice versa. The popular view is that Prakrit, is the corrupted version of Sanskrit. The Shramanas however reverse the stand saying Sanskrit is the refined version of Prakrit.</p>
<p class="text-h6">
Ardha Magadhi Prakrit of the Jainas</p>
<p>
<q-img src="../../assets/assorted/adinatha.jpg" width="150px" class="float-left q-ma-md"/>
Lets begin with the naked Nigranthas.. 🙂 The liturgical language of the Jainas is Ardha Magadhi. Though at a very late stage, they did switch to Sanskrit but still their ancient liturgical texts were still in Ardha Magadhi. In fact, their usage of Prakrit continued even after majority of the Buddhist sects began to adapt Sanskrit (or at the least Prakritic Sanskrit a.k.a Buddhist Hybrid Sanskrit). The Jainas use of Prakrit mantras and verses for chanting and rituals were even criticized by the Brahamanists.</p>
<p>
For Instance, the 8th century Vedic Shaivaite Tamil saint Sundarar, one of the 63 Shaivaite Nayanmars (See: 63 Jaina Shalaka Purushas and Nayanmars) criticizes the Jains as follows:</p>

<p class="blockquote">
<i>
நமண நந்தியும் கரும வீரனுந்<br/>
தரும சேனனு மென்றிவர்<br/>
குமணன் மாமலைக் குன்று போல்நின்று<br/>
தங்கள் கூறையொன் றின்றியே<br/>
ஞமண ஞாஞண ஞாண ஞோணமென்<br/>
றோடி யாரையு நாணிலா<br/>
அமண ராற்பழிப் புடைய ரோநமக்<br/>
கடிக ளாகிய வடிகளே<br/>
<br/>
namaṇa nantiyuṅ karuma vīraṉun<br/>
taruma cēṉaṉu meṉṟivar<br/>
kumaṇaṉ māmalaik kuṉṟu pōlniṉṟu<br/>
taṅkaḷ kūṟaiyoṉ ṟiṉṟiyē<br/>
ñamaṇa ñāñaṇa ñāṇa ñōṇameṉ<br/>
ṟōti yāraiyu nāṇilā<br/>
amaṇa rāṟpaḻip puṭaiya rōnamak<br/>
kaṭika ḷākiya vaṭikaḷē.</i>
</p>
<p class="blockquote">Those Jainas with names such as Namana Nandi, Karma Vira, Dharmasena standing like [insignificant] hills near mountains, being naked chant mantras like “ñamaṇa ñāñaṇa ñāṇa ñōṇa” (meaningless string of letters using the nasals to mock the Prakrit usage of Jainas) without any shame in anyone's presence. Can our lord Shiva be the object of contempt of such Jainas?</p>
<p>Here we can see that the Tamil Shaivaite saint makes a mockery of the the heretic Jainas’ usage of Ardha Magadhi, as Prakrit languages tend to use nasal sounds more. Even in several other verses, the Nayanmars criticize the Jainas for using the corrupted language of Prakrit instead of the perfect language of the Agamas i.e Sanskrit</p>

<p>The Jainas had no reasons to crouch, of course. For according to them, Ardha Magadhi was the divine language, the original language of all. The language that was spoken by Mahavira, was also spoken by the [Jaina] gods and goddesses. It is thus said in the Jainas’ Aupapatika Sutra:
</p>
<p class="blockquote">
<i>bhagavam mahāvīre […] savvabhāsāṇugāmiṇīe sarassaīe joyaṇanīhāriṇā sareṇaṁ addhamāgahāe bhāsāe bhāsai […] sā vi ya ṇaṁ addhamāgahā bhāsā tesiṁ savvesiṁ āriyam aṇāriyāṇaṁ appaṇo sabhāsāe pariṇāmeṇaṁ pariṇamai</i></p>
<p class="blockquote">With a voice that extends over a yojana, Lord Mahavira speaks in the Ardha-Magadhi language, a speech which is in accordance with all languages. That Ardha-Magadhi language changes into the own language of all those, both aryas and non-aryas.</p>

<p>A 11th century Jain Author goes to the extent of considering Prakrit as the source of Sanskrit. He states: </p>

<p class="blockquote"><i>[…]bāla mahilādi subodhaṁ sakala bhāṣāni bandhanabhūtaṁ vacanam ucyate | meghanirmuktajalam ivaikasvarūpaṁ tad eva ca deśaviśeṣāt saṁskārakaraṇāc ca samāsāditaviśeṣaṁ sat saṁskṛtādyuttaravibhedān āpnoti |[…]</i></p>

<p class="blockquote">
[…] [Prakrit] is said to be a language easy to understand for children and women, the origin of all languages. Like the water released by a cloud, it has but one form, yet, once differences have entered because of the difference between regions and because of beautification, it acquires the later distinctions between Sanskrit and the other languages. […]</p>

<p>Therefore, its quite clear that the Jainas consider their Prakrit Ardha Magadhi as the original language. Accordingly, all languages have indeed evolved from Ardha Magadhi.</p>

<p class="text-h6">Magadhi Prakrit (Pali]) of the Thervadin Buddhists</p>
<q-img src="../../assets/assorted/theravada.jpg" width="150px" class="float-left q-ma-md"/>
<p>Of all the Buddhists traditions, Theravada was the only sect to preserve the usage of the Magadhi Prakrit in its literature. The term “Pali” was traditionally used for denoting the texts in the language. The language itself is referred to as Magadhi in the Theravadin literature. Even though Theravada is prevalent only outside India, the usage of Pali is still strong among the native Buddhists. This serves as the strong example for the bonding between the Magadhi Language & Theravada.</p>

<p>Similar to the Jainas, the Theravadins also held the view that their language is the most natural  and original language. The fifth century Visuddhimagga by Buddhaghosa declares:</p>

<p class="blockquote">sā māgadhī mūla bhāsā nārā yā yādi kappikā
brahmānochassutālāpā sambuddhā ehāpi bāsare</p>

<p class="blockquote">Magadhi is the root of all dialects, which was spoken by Brahmas, by men before the present kalpa, by those who had neither heard nor uttered human accent, and also by supreme Buddhas</p>

<p>The atthagatha (commentary) of <a>abhidhamma vibhanga</a> states the below as the view of the Buddhist monk Tissadatta Thera:</p>

<p class="blockquote">If a child, born of a Damila [Tamil] mother, and an Andhaka [Telugu] father, should first hear his mother speak, he would speak the Damila language ; but if he should first hear his father speak, he would speak the Andhaka language. If however he should not hear them both, he would speak the Magadhl If, again, a person in an uninhabited forest, in which no speech (is heard), should intuitively attempt to articulate words, he would speak the very Magadhi.

It predominates in all regions (such as) Hell; the animal kingdom; the Preta sphere ; the human world ; and the world of the devas. The rest of the eighteen languages—Otta, Kirata, Andhaka, Yonaka, Damila, etc., undergo changes —but the Magadhi does not, which alone is unchangeable, and is said to be the speech of Brahmans and Ariyas.

Even Buddha, who rendered his tipitaka words into texts, did so by means of the very Magadhi ; and why ? Because by doing so it (was) easy to acquire their (true) significations. Moreover, the sense of the words of Buddha which are rendered into doctrines by means of the Magadhi language, is conceived in hundreds and thousands of ways by those who have attained the patisambhida, so soon as they reach the ear, or the instant the ear comes in contact with them ; but discourses rendered into other languages are acquired with much difficulty.</p>

<p>The view that Pali is immutable is so Sanskrit-like. I have heard many Sanskrit enthusiasts claim that Sanskrit has never undergone any change and is pristine without any modification and alterations, unlike other regional languages and, is therefore, perfect. As a side note, I don’t know why perfect languages should never change. I am of the belief that anything natural should under go change and evolve.</p>

<p>This view got quote developed and the 11th century grammar treatise Mohavicchedani states:</p>

<p class="blockquote"><i>sā va apāyesuu manusse devaloke c’eva paṭhamam ussannā | pacchā ca tato andhaka yonaka damiḽādi desabhāsā c’eva sakkaṭadi aṭṭhārasa mahābhāsā ca nibattā |</i></p>

<p class="blockquote">It (i.e. Magadhi) was first predominant in the hells and in the world of men and that of the gods. And afterwards the regional languages such as Andhaka, Yonaka, Damila, etc., as well as the eighteen great languages, Sanskrit, etc., arose out of it.</p>

<p>As the grand finale, here too, the view converges to considering Pali as the source language of all. One interesting point to note that it even considers the Greek language (Yonaka) as being born from Pali./p>

<p class="text-h6">Buddhist Sanskrit of Mahayana</p>
<q-img src="../../assets/assorted/chundi.jpeg" width="150px" class="float-left q-ma-md"/>
<p>Sanskrit, or to be precise Buddhist Hybrid Sanskrit is the liturgical language of Mahayana Buddhism. In fact, at all the sutras and shastras of Mahayana are completely in Sanskrit or precisely speaking, in the Buddhist variant. Buddhist Hybrid Sanskrit is sort of a Prakritized Sanskrit. It is a unique Sanskrit variant with several prakritic elements and imperfections (in a Paninian context), what one may consider as Vernacular Sanskrit.</p>

<p>Though Mahayana itself doesn’t attribute any special status to the Sanskrit language. Johannes [1] suggests that some Buddhists might have considered that Buddhist Hybrid Sanskrit as the original source language. Well, I don’t actually agree with it. As the author notes, Mahayanists didn’t seem to consider their Sanskrit different from the Sanskrit of the Brahmanists.  It makes no sense for them to claim their Sanskrit was the source for the Brahmanical Sanskrit. Also, at one point of time, they completely switched from their Hybrid Sanskrit to Classical Sanskrit.</p>

