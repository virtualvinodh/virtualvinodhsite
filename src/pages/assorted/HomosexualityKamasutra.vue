I have been wanting to write about this for several years but never got around to writing anything at all. It was 9 years ago (I am 26 now) when I first read the Kamasutra completely. While reading the book, I came across the chapter on “oral sex” (auparishtaka) with references to what appeared as oral sex between men in ancient India. Even though it was more of a passing reference that just bore witness to the fact that such practices already existed in ancient India. It was totally non-judgemental and no eternal damnation of any sort was specified, which was in a way personally very reassuring for me. Perks of following a dharmic religion, I suppose. young-me surely didn’t want to go to hell. At that time, I didn’t have enough Sanskrit to actually go to the source and its commentary and analyze the chapter in detail. But I suppose 9 years later I can do that now!</p>
<p>A close reading of the related verses and its subsquence analysis would be certainly helpful to derive and discuss some actual facts as described in the text itself, which is our primary source. Let’s dive into the source and see what the text itself says about male homosexual practices and perhaps try to make some interesting inferences, observations and analysis along the way. This article focuses mainly on male homosexual practices, I’d assume female homosexual practices will be discussed in future articles.</p>

<p>Now, some briefing about Kamasutra. I’d guess everyone has at least heard about Kamasutra. The text was probably composed around 400 BCE and 200 CE by Vātsyāyana. The extant version consists of 8 sections. The chapter on oral sex (<i>aupariṣṭaka</i>) occurs as the 9th chapter in section 2 on sexual union (<i>sāṃproyogika</i>). There appears to have been several commentaries (<i>vyākhyas</i>) to Kamasutra. Perhaps, one the most important is the <i>Jayamaṅgala</i> commentary written by Yaśodhara around 12th century CE. There is also a contemporary Hindi commentary called Jaya written in the 20th century. But I don’t have acccess to it. So, we would be sticking to Kamasutra and its Jayamangala commentary.</p>

<p>In this article, we would particularly be doing a close reading of verse 36 in the 9th chapter of section 2. So let’s dive in!</p>

<p class="blockquote"><i>tathā nāgarakāḥ kecidanyonyasya hitaiṣiṇaḥ | <br/>
kurvanti rūḍhaviśvāsāḥ parasparaparigraham ||</i><br/>
<br/>
And, in the same way (<i>tathā</i>), certain city-dwelling-men (<i>kecid nāgarakāḥ</i>) who desire for one another’s welfare (<i>anyonyasya hitaiṣiṇaḥ</i>) and have established-trust (<i>rūḍha-viśvāsāḥ</i>) do (<i>kurvanti</i>) this service [oral sex] for one another (<i>paraspara-parigraham</i>).<br/>
<br/>
— Verse 2.9.36</p>

<p>The apparent meaning is clear, in ancient India there were men who performed oral sex among themselves. But is that just it or can we read something more  that underlies the given verse? Let’s go through the major phrases in detail one by one.</p>

<p><i>hitaiṣiṇaḥ</i> &lt; <i>hita</i> + <i>eṣiṇaḥ</i> (those who desire/seek). hita is a very overloaded term that implies “wellness”, “welfare”, “beneficial” etc.  The term has a general connotation of “goodness”/”wholesomeness”. <i>anyonyasya hita-eṣiṇaḥ</i> – Those who desire/seek “goodness” of one another (<i>anyonya</i>). These men (actively) sought/desired “wellness”/”goodness” (not just for themselves) but (also) for each other. This implies that they were always caring for each other.  But Jayamangala’s gloss for this phrase is a bit more directly related to the sexual activity – <i>visṛṣṭi-sukha-kāritvāt</i> – “due to causing the pleasure of emission (of semen)”. Jayamangala clearly only sees the sexual connotation to the word. But as we saw earlier, hita is a very subtle terminology that would hardly mean the “pleasure of ejaculation“.</p>

<p><i>rūḍhaviśvāsāḥ</i> &lt; <i>rūḍha</i> + <i>viśvāsāḥ</i> (those with <i>viśvāsa</i>). rūḍha has a connotation of conventional/established. In Sanskit etymology, <i>rūḍha</i> refers to words whose meaning can’t be derived from its components and can only be inferred for conventional/established usage. viśvāsa has the associated meanings of “trust”, “belief” and “confidence”. rūḍhaviśvāsāḥ refers to those with established/certain trust/belief (in each other). Jayamangala glosses <i>rūḍhaviśvāsā</i> as <i>maitryā</i> (with friendship). They also had trust and belief which had been (strongly and previously already) established amongst themselves. The <i>pāṭhabheda</i> (variant) reading of the phrase is <i>gūḍhaviśvāsāḥ</i> – “private/secret trust”, which also fits well in this context.</p>

<p>As we can see, the terms that Vatsyayana uses – <i>anyonyasya hitaiṣiṇaḥ</i> (mutual well-being-desiring) and <i>rūḍhaviśvāsāḥ</i>(established-trust) quite strongly suggests that they were not just acquaintances. They were surely something more than that. So, the two men with such mutual qualities perform <i>paraspara-parigraha</i>.  Paraspara is the same as anyonya meaning “mutual”. Parigraha can refer to “service”, “favour”, “help”, “assistance”, “receiving”, “accepting” etc. The general connotation is something that is received.  They were doing (<i>kurvanti</i>) a mutual favor/service/assistance for each other.  We can also see that vātsyāyana uses the word “mutual” (<i>anyonya</i> & <i>paraspara</i>) explicitly twice in the same verse.</p>

<p>The verse certainly means that such men were “partners” in a contemporary sense. The phrases used to describe the relationship clearly points in that direction. Jayamangala’s gloss “with friendship” gives an idea they were probably considered as “friends” in the ancient world. So were they actually “partners” or just “friends with benefits”? We may never know! But practically speaking I suppose they were of both kinds. But the verse clearly shows that it was much more than a one-off horny encounter between men. The other thing to notice is the term <i>nāgaraka</i> (city-dwelling-men). In the ancient world, men residing in cities were considered very civilized, sophisticated and refined (I suppose it still holds true in a way). By using the term <i>nāgaraka</i>, Vatsyayana inadvertently mentions that this was practised by sophisticated and refined men!</p>

<p>Vatsyayana uses the word “certain men” (<i>kecid</i>) in this verse. It appears that these men were not having oral sex with everyone. They were performing oral sex with men with similar (homosexual) tendencies. In an indirect way, Vatsyayana thus also seems to recognise the fact that a section of men had such (homosexual) tendencies. <i>Kecid</i> is glossed as <i>yoṣāprāyāḥ</i> : “those who are mostly-women” in Jayamangala. The term might refer to very effeminate men.   It  is mostly because the commentator might have thought that homosexual men were all effeminate. Do note that the commentary is dated circa 800 years ago. Well... Some things have never changed in 800 years!</p>

<p>So, how did these men actually perform this mutual service? We move on to Jayamangala for the details:<p>

<p class="blockquote"><i>mama tāvatkuru paścāttavāpi kariṣyāmiti | yugapadvā dehavyatyā – sena rāgātkālamanapekṣamāṇāviti dvividham |</i><br/><br/>

(They say,) ‘You do it for me now, and I will do it for you later.’ (Or both of them do it at the same time), by turning their bodies head to foot, losing all sense of time because of their passion. (Thus) there are two ways (of doing it).</p>

<p>They were not looking about the time (<i>kālam-anapekṣamāṇau</i>) because of their passion (<i>rāgāt</i>). They were not caring about the time while they were involved in the activity.  There appears to have been such a strong passion among them they just lost all sense of time while they were indulging oral sex in that position.</p>

<p>Well... what about women... you ask,<p/>

<p class="blockquote"><i>strīyo’pi kurvanti | yathoktam – ‘antaḥpuragatāḥ kāścidaprāptabhāṇḍakāḥ(?) striyaḥ | bhage hyanyonyaviśvāsātkurvanti mukhacāpalam ||’ iti |</i>
<br/><br/>
Women, too, can do this. As it is said: ‘Certain women in the harem, unable to get any tools, trusting in one another, excite one another with their mouth on the vagina.’</p>

<p>The most interesting phrase is “unable to get any tools” (<i>aprāpta-bhāṇḍakāḥ</i>). What sort of tools (<i>bhāṇḍaka</i>) were they using? Ancient Indian sex-toys anyone?</p>

<p>Now, it is pretty much clear that some “civilized” men who were considered “friends” by society were most probably in a serious gay relationship with other men of similar tendencies. Their relationship was full of “passion” that firmly involved “seeking mutual-welfare of each other” and “established-trust among themselves”. This verse strongly establishes without doubt that serious homosexual relationships existed in ancient India.  It is not a result of westernization or a matter of recent western import. This is something that’s been part of Indian culture since ancient times. The amount of conspiracy theories and mis-information that one comes across relating to homosexual practices in India is mind boggling. I suppose this verse clearly denies them and establishes the contrary.</p>

<p>As Vatsyayana concludes the chapter with the verse,</p>

<p class="blockquote"><i>arthasyāsya rahasyatvāccalatvānmanasastathā | <br/>
kaḥ kadā kiṃ kutaḥ kuryāditi ko jñātumarhati ||<br/></i>
<br/>
But because this matter is secret, and because the mind and heart are fickle,<br/>
who could know who should do what, and when and how?</p>

<p>Of course this is referring to the general practice of oral sex. But it does make perfect sense here in the context of male homosexual activities (being a subset of the former). This was a very private matter that no should/could possibly care/know about!</p>

