        <p><small><i>Composed: Feb, 2010. Updated: Sep, 2020</i></small></p>
<p>During the days when E-Sangha, was up and active, there was a thread on Hari Hari Hari Vahana Lokeshara, one of the 108 forms of Avalokitesvara. I had come across this interesting form of Avalokitesvhara from the book titled “Indian Buddhist Iconography”. So I started a thread on about him, where lot of information had been gathered about this peculiar form of Avalokiteshvara (mostly by me). Given that E-Sangha is down, I thought I could replicate the information here.</p>
<q-img src="../../assets/assorted/harihari-1.jpg" width="180px" class="float-left q-ma-md"/>

<p>The author of Indian Buddhist Iconography quotes Sādhanāmāla to describe this form of Avalokitesvara as follows:</p>

<p class="blockquote"><em>harihariharivāhanodbhavaṁ bhagavantaṁ ārya-avalokiteśvaraṁ sarvāṅgaśuklaṁ jaṭāmukuṭinaṁ śāntaveśaṁ dakṣiṇakareṇa bhagavantaṁ tathāgataṁ sākṣiṇaṁ kurvantaṁ dvitīyena akṣamālādhārṇaṁ tṛtīyena duḥkuhakaṁ lokaṁ upadeśayantaṁ vāmena daṇḍadharaṁ dvitīyena kṛṣṇājinadharam tṛtīyena kamaṇḍaladharaṁ siṁha-garuḍa-viṣṇu-skandhasthitaṁ ātmānam dhyātvā [...]</em></p>

<p class="blockquote">The worshipper should think himself as the Harihariharivahana form of Avalokiteshvara, white in all limbs, with the Jatamukuta, and clad in graceful garments. He cites the Tathagata as witness with one of his right hands, carries the rosary in the second, and instructs deluded people with the third. He carries the staff in one of his left hands, the deer skin in the second and the Kamandalu in the third. He sits on the shoulder of Vishnu below whom there are Garuda and the lion. Thus meditating [..]</p>

<p>The text "The Iconography of Nepalese Buddhism" describes the story behind Hari Hari Hari Vahana Lokeshvara.</p>
<q-img src="../../assets/assorted/harihari-2.jpg" width="250px" class="float-left q-ma-md"/>

<p class="blockquote">This is one of the most interesting sculptors/forms of Avalokiteshvara. It seems that this form of Avalokiteshvara is basically of Nepalese origin. According to Swayambhu Purana, when the great bodhisattva Manjushree drained the lake called Nagarhad (now Kathmandu valley) all the Nagarajas especially Takshaka were very angry. Bodhisattva Samantabhadra pacifed them all except the Takshaka. He killed many people and animals. Due to this act of violence he was aficted with leprosy. He repented his actions and began to pray for Arya Avalokiteshvara in order to eliminate his bad karma. Due to his strong devotion to Lord Avalokitesvara he was cured of his disease.<br/><br/>

One day while he was leisurely sun basking in a nearby river, a hungry Garuda saw him there. He alighted down and tried to devour Takshaka. Since Takshaka was the king of Nagas, he had great strength. They began to engage in a ferocious battle. He soon began to drag Garuda into river and intended to kill him. Garuda, fnding himself about to be killed, invoked his protector Vishnu who came there immediately to save him. As Vishnu was about to cut of the head of Takshaka with his Sudarsanacakra i.e. whirling disc, Takshaka, realizing his upcoming defeat, prayed for Avalokiteshvara for his protection. Avalokiteshvara arrived there instantly fying over his lion vehicle. Vishnu was ashamed to fnd Avalokiteshvara, Lord of compassion in front of him and instantly bowed before him throwing away his Sudrashan Chakra and paid homage.<br/><br/>

Thereafter Takshaka and Garuda too retreated from the battle and joined to the worship of Avalokiteshvara. After that Vishnu offered himself to Avalokiteshvara to be his vehicle. In the mean time, the lion also offered himself to be the vehicle of Garuda, in turn. Nagaraja Takshaka also offered to be the vehicle of the lion. Thus this composite image of Avalokiteshvara on Vishnu, and on Garuda, then on the lion, all then on Takshaka.</p>

<p>All the three, namely, Garuda, Maha Vishnu, and the lion, have the epithet of “Hari”. Since these three Haris serve as the vahana (vehicle) of Avalokiteshvara,  this composite image came to be known as Hari-Hari-Hari-Vahana Lokeshvara.</p>

<p>When the followers of the Vedic religion claimed Buddha to be an avatar of Vishnu, it is interesting to note that here Vishnu is shown to be completely sub-ordinate to bodhisattva Avalokiteshvara. Maha Vishnu feels ashamed to have wielded his weapon i.e Sudarshana Chakra before Avalokiteshvara and instantly bows down before him. In another instance, Maha Vishnu worships the Buddha and asks for a Dharani to secure victory in battles, which we shall discuss later.</p>
