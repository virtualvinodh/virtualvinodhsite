<p>தேரவாத பௌத்தர்களும், மாகதி மொழியே, மிகவும் இயல்பான மொழி என்றும் மூல மொழி என்றும் கருதிக்கொண்டனர்.</p>

<p>ஐந்தாம் நூற்றாண்டை சேர்ந்த புத்தகோசர் என்னும் ஆசிரியர் தம்முடைய விசுத்திமாக்கம் என்று நூலில் விளக்குகிறார் –</p>

<p class="blockquote">ஸா மாக³தீ⁴ மூல பா⁴ஸா நாரா யா யாதி³ கப்பிகாப்³ரஹ்மாநோச²ஸ்ஸுதாலாபா ஸம்பு³த்³தா⁴ ஏஹாபி பா³ஸரே</p>

<p class="blockquote">அந்த மாகதியே மூல பாஷையாகும், முந்தைய கல்பத்தில் (யுகத்தில்) இருந்த மக்களாலும், பிரம்மதேவர்களாலும், மொழியே கேட்காதவர்களாலும் பேசாதவர்களாலும், முழுமையடைந்த புத்தர்களாலும் (இதுவே) பேசப்பட்டது</p>

<p>விபங்கம் என்ற பௌத்த அபிதர்மத்தின் உரை ஒன்று, திஸ்ஸதத்த தேரர் என்று புத்த பிக்ஷுவின் கருத்தாக பின்வருபவனற்றை கூறுகிறது:</p>

<p class="blockquote">
“தமிழ் தாய்க்கும், அந்தக (தெலுங்கு) தந்தைக்கும் பிற குழந்தையானது, தன்னுடைய தாயின் பேச்சை முதலில் கேட்டால், தமிழ் மொழியை பேசும்; ஒருவேளை தந்தையின் பேச்சை முதலில் கேட்டால், தெலுங்கு மொழியில் பேசும். எனினும், அவர்களிருவரையும் கேட்காதிருந்தால், (தானாக) அக்குழந்தை மாகதி மொழியில் பேசும். மீண்டும், பேச்சை கேட்க்காது தனித்து காட்டில் வாழும் ஒருவர், பேசமுற்பட்டால், அவர் இதே மாகதி மொழியில் தான் பேசுவார்.
<br/><br/>
மாகதி மொழி அனைத்து இடங்களில் பிரதானமாக உள்ளது: நரகம், விலங்குகளின் ராஜ்யம், பிரேத லோகம், மனுஷ்யலோகம் மற்றும் தேவலோகம். மற்ற பதினெட்டு மொழிகளான – ஒட்டம், கிராடம், தெலுங்கு, யோனகம், தமிழ் முதலிய மொழிகள் மாற்றம் பெருகின்றன, ஆனால் மாகதி மட்டுமே மாற்றம் பெறுவதில்லை, இதுவே பிரம்மதேவர்களின் மொழியாகவும் ஆரியர்களின் (உயர்ந்தோர்) மொழியாகவும் உள்ளது.
<br/><br/>
புத்தரும்,  தம்முடைய திரிபிடகத்தை வார்த்தைகளாக வெளியிட்டது இதே மாகதி மொழியினாலேயே ஏன் ? அவ்வாறு செய்ததினால், அவற்றின் உண்மையன அர்த்தத்தை அறிந்துகொள்ளுதல் எளிது. இன்னும், புத்தரின் வார்த்தகளின் அர்த்தம், போதனைகளாக மாகதி மொழியில் வெளியிடும் போது பதிசம்பிதை (ஓர் உயர்ந்த நிலை) எய்தியவர்களின், காதுகளை எட்டிய உடனே, அது நூற்றக்கணக்கான ஆயிரக்கணக்கன விதமாக புரிந்துகொள்ளப்படுகிறது. ஆனால், மற்ற மொழிகளில் வெளியிடப்படும் போதனைகள் மிகக் கடினமாகவே பெறப்படுகின்றன”.</p>

<p>இது போன்ற கருத்துக்கள் மிகவும் வளர்ந்த நிலையில், 12ஆம் நூற்றாண்டு இலக்கண நூலான மோஹவிச்சேதனி கூறுகிறது,</p>

<p class="blockquote">
ஸா வ அபாயேஸு மநுஸ்ஸே தே³வலோகே சேவ பட²மம் உஸ்ஸந்நா |  பச்சா² ச ததோ அந்த⁴க யோநக த³மிளாதி³ தே³ஸபா⁴ஸா சேவ ஸக்கடதி³ அட்டா²ரஸ மஹாபா⁴ஸா ச நிப³த்தா |</p>

<p class="blockquote">
இது (மாகதி) நரகத்தில், மனுஷ்யலோகத்தில் மற்றும் தேவலோகத்திலும் பிரதானமாக இருந்தது. பின்னர், தெலுங்கு, யோனகம், தமிழ் முதலிய பதினெட்டு தேசபாஷைகளுடன் சமஸ்கிருதமும் இதில் இருந்தே தோன்றின</p>

<p>இங்கும் கூட மாகதி (பாளி) மொழியே முதலிய அனைத்து மொழிகளின் மூல மொழியாக தேரவாத பௌத்தர்களால் கருதப்படுவது புலனாகிறது. இதில் இந்திய மொழிகளோடு கிரேக்கத்தையும் (யோனகம்) சேர்த்து இருப்பது வியப்பே.</p>

<p>முக்கியமாக இங்கு கவனிக்க வேண்டியது, புத்தகோசரும் மோஹவிச்சேதனியின் ஆசிரியாரான காஸ்ஸபரும் காஞ்சிபுரத்தை இருப்பிடமாக கொண்டு நூல்களை இயற்றியவர்கள்.</p>

<p>இவ்வாறாக ஏதோ ஒரு காலக்கட்டத்தில் ஒவ்வொரு மதமும் அதைச்சார்ந்த மொழியே அனைத்திற்கும் மூலம் என்று கருதிவந்துள்ளன. எல்லா மதங்களும் மொழிகளை (தம்) மதம் சார்ந்த பார்வையோடு தான் மொழிகளை கண்டு வருகின்றன. தம்முடைய மதமே பிராதனம் என்றும் அனைத்திற்கும் மூலம் என்ற எண்ணத்தின் நீட்சியாகவே,  தாம் சார்ந்த மதத்தின் மொழியே மூலமொழி என்ற எண்ணமாக வெளிப்பட்டி இருக்க வேண்டும்.  மதம் சார்ந்த சிந்தனைகளுடைய பரிணாமத்தின் ஒரு பங்கே இவை அனைத்தும்.</p>

