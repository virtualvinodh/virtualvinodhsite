    <p class="text-h5">சிரமணர்களின் மூல மொழி

</p>
    <p><small><i>Composed: December, 2010</i></small></p>
<q-img src="../../assets/tamil/dharmachakra.png" class="float-left q-ma-sm" width="150px"/>
<p>வைதிக சமயத்தவர்கள் பொதுவாக சமஸ்கிருத மொழியையே, அனைத்து மொழிகளுக்கும் மூலமாகவும், அனைத்தும் அதில் இருந்தே கிளைத்ததாகவும் கருதி வந்துள்ளனர். இதே போல தமிழ்த்தேசியவாதிகளும் தமிழ் தான் உலகமுதன்மொழி என்றும் உலகமொழிகள் அனைத்தும் தோன்றியதாக கருத்தை முன்வைக்கின்றனர். எப்படியாகிலும் சரி, எந்த மொழியையும் உலகின் ஆதிமொழியாக கருதிக்கொள்ள இயலாது. இந்த உலக முதன்மொழி என்ற கருத்தே விவாதத்திற்குரியது தான். பலரும் இந்த கருத்தை ஏற்றுக்கொள்வதில்லை. மொழிகள் ஒரே காலக்கட்டத்தில் வெவ்வேறு இடங்களில் இணையான தோற்றம் பெற்று, பின்னர் தம்முள் கலந்து உறவாடி நிலைபெற்றன என்பது இன்னொரு கருதுகோள். இதை Polygenesis (பல்தோற்றம்) என்று கூறுவர்.</p>

<p>வைதிகர்கள் வேத பாஷையை அனைத்து மொழிகளின் மூலமாகவும் தெய்வ பாஷையாகவும் கருதியதில் வியப்பேதுமில்லை. வைதிகர்களின் நம்பிக்கையின் படி, வேதங்கள் அபௌருஷேயமானவை [மனிதர்களால் இயற்றபடாதது]. பிரபஞ்ச ஒலிகளை வேத சுருதிகளாக வடிப்பதற்கான கருவிகளாகத்தான் ரிஷிகள் இருந்தனராம். அவை அநாதியானவையும் கூட. இது போன்ற அதீத குணங்களை கொண்ட வேதங்கள் இருக்க, அவை இயற்றப்பட்ட மொழியும் அதே போல அதீதமாகவும் தெய்வீகமாகவும் இருப்பது தானே முறை ?</p>

<p>இது இவ்வாறிருக்க, இனி சிரமண சமயங்களான பௌத்த ஜைன மதங்களுக்கு வருவோம். அவை மக்களிடம் புழக்கத்தில் இருந்த பிராகிருத மொழிகளை பிரதானமாக கொண்டே எழுந்தவை. மக்களின் மொழியையே பயன்படுத்தி தம்முடைய கருத்துக்களை பிரச்சாரம் செய்து இம்மதங்கள் பரப்பின. மக்களிடைய செல்வாக்கு பிற இதுவும் ஒரு முக்கிய காரணமாயிற்று (பௌத்த-ஜைன மதங்கள் தோன்றிய காலக்கட்டத்தில் சமஸ்கிருத மொழி சாஸ்த்ர-ராஜ பாஷையாக பரிணமிக்கவில்லை, பிராமணர்களின் மொழியாகத்தான் முடங்கி கிடந்தது ). ஒரு சமயத்தில், புத்த பகவான் தன்னுடைய போதனைகளை சந்தஸில் [வேத மொழி] சேமிக்கப்படகூடாதென்று தடுத்தார். மக்களின் மொழியிலேயே அவை இருக்க வேண்டும் என்று தெரிவித்தார். ஆனால், காலம் செல்ல செல்ல முற்றிலும் முரணாக சிரமண சமயத்தவர்களே கூட தம்மைச்சார்ந்த மொழியையே மூல மொழியாக கருதத்தொடங்கினர்.</p>

<p>பொதுவாக பிராகிருதம் சமஸ்கிருதத்தின் திரிந்த வடிவம் என்ற கருத்துள்ளது. பிராகிருதத்துக்கும் சமஸ்கிருதத்துக்கு உள்ள வேறுபாடு, கிட்டத்தட்ட கொடுந்தமிழுக்கும் (பேச்சுத்தமிழ்) செந்தமிழுக்கும் உள்ள வேறுபாடுதான். [பேச்சுத்தமிழை எழுதினாலே அது இன்னொரு மொழிபோலத்தான் இருக்கும்]. ஆனால், சிரமணர்களோ, இதற்கு மாற்றாக பிராகிருதத்தின் திருத்தப்பட்ட வடிவமே சமஸ்கிருதம் என்ற கருத்தை கொண்டிருந்தனர் (இதே ரீதியில்,  செந்தமிழ் பேச்சு வழக்கினால் மக்களால் திரிக்கப்பட்டு கொடுந்தமிழ் ஆனதா, அல்லது மக்களிடம் புழங்கிய திருத்தம் பெறாத கொடுந்தமிழ்(கள்)  இலக்கணம் சமைக்கப்பெற்று சீர்தரம் செய்யப்பட்டு செந்தமிழ் ஆனதா என்பது விவாதிக்க வேண்டிய விஷயமே )</p>

<p>இனி பௌத்த ஜைன மதங்கள் மூலமொழி குறித்து என்னென்ன கருத்துக்களை கொண்டிருந்தன என்பதை பற்றியும் விரிவாக காண்போம்.</p>

<p class="text-h6">ஜைனர்களின் அர்த்தமாகதி பிராகிருதம்</p>
<p>ஜைன சமயத்துடன்  துவங்குவோம். ஜைனர்களின் மத வழிபாட்டு மொழியாக இருந்தது அர்த்த மாகதி மொழி. பிற்காலத்தில் பௌத்த மதப்பிரிவுகள் பெரும்பாண்மையானவை சமஸ்கிருதத்தை ஏற்றுக்கொண்ட நிலையிலும், ஜைனர்கள் பிராகிருதத்தை கைவிடவில்லை. மிகவும் பின்னரே அவர்கள் சமஸ்கிருதத்துக்கு மாறினர் (இருப்பினும் அவர்களின் மூல நூல்கள் அர்த்த மாகதியிலேயே இருந்தன). ஜைனர்களின் இந்த பிராகிருத பயன்பாடு, வைதிகர்களின் விமர்சனத்துக்கும் ஏளனத்துக்கும் உட்படுத்தபட்டது.</p>

<p>உதாரணமாக, 8ஆம் நூற்றாண்டை சேர்ந்த 63 நாயன்மார்களில் ஒருவரான சுந்தரர், ஜைனர்கள் குறித்து இவ்வாறு பாடுகிறார்.</p>

<p class="blockquote">நமண நந்தியுங் கரும வீரனுந்
<q-img src="../../assets/tamil/mahavira-1.jpg" class="float-left q-ma-sm" width="100px"/>
<br/>
தரும சேனனு மென்றிவர்
<br/>
குமணன் மாமலைக் குன்று போல்நின்று
<br/>
தங்கள் கூறையொன் றின்றியே
<br/>
ஞமண ஞாஞண ஞாண ஞோணமென்
<br/>
றோடி யாரையு நாணிலா
<br/>
மண ராற்பழிப் புடைய ரோநமக்
<br/>
கடிக ளாகிய வடிகளே</p>

<p class="blockquote">நமண நந்தி, கர்ம வீரன், தர்ம சேனன் என்றவாறு பெயர்களைக்கொண்டு, குமணன் என்ற பெருமலைக்கு அருகில் இருக்கும் குன்றுகளைபோல, ஆடையின்றி “ஞமண ஞாஞண ஞான ஞோண” என்று நாணமில்லாது உச்சாடனம் செய்யும் ஜைனர்களால் சிவன் பழிக்கப்படுதலுக்கு உடையரோ ?</p>

<p>பொதுவாகவே பிராகிருத மொழிகளில் மெல்லின பிரயோகங்கள் அதிகமாக இருக்கும். எனவே, அவர்களின் மந்திரங்களை மேற்கண்டவாறு (ஞமண ஞாஞண ஞான ஞோண)  ஏளனம் செய்கிறார் சுந்தரர். வேறு சில பாடல்களிலும், ஆகமங்களின் மொழியை (சமஸ்கிருதம்) விடுத்து திரிந்த பிராகிருத மொழியை பயன்படுத்துவதை விமர்சிக்கின்றனர் நாயன்மார்கள்.</p>

<p>இதனால் அக்கால ஜைனர்கள் கவலை ஏதும் பட்டிருக்கப்போவதில்லை, அர்த்த மாகதி தான் அவர்களுடைய தேவ பாஷை ஆயிற்றே. ஜைனர்களின் படி, அர்த்த மாகதியே தெய்வ பாஷையும் மூல பாஷையும் ஆகும். இம்மொழியே வர்த்தமான மஹாவீரராலும், மற்ற (ஜைன) தேவதாமூர்த்திகளாலும் உபயோகப்படுத்தப்படுவது.ஜைனர்களின் ஔபாதிக சூத்திரத்தில் இவ்வாறு காணப்படுகிறது:</p>

<p class="blockquote">ப⁴க³வம் மஹாவீரே […] ஸவ்வபா⁴ஸாணுகா³மிணீஏ ஸரஸ்ஸஈஏ ஜோயணநீஹாரிணா ஸரேணம் அத்³த⁴மாக³ஹாஏ பா⁴ஸாஏ பா⁴ஸை […] ஸா வி ய ணம் அத்³த⁴மாக³ஹா பா⁴ஸா தேஸிம் ஸவ்வேஸிம் ஆரியம் அணாரியாணம் அப்பணோ ஸபா⁴ஸாஏ பரிணாமேணம்  பரிணமை</p>

<p class="blockquote">பகவான் மஹாவீரர் […] அனைத்து பாஷைகளுக்கும் அனுசரனையாக இருக்கக்கூடிய, அர்த்த மாகதி பாஷையை, ஒரு யோஜனை வரை கேட்கக்கூடிய குரலில் பேசுகிறார் […] அந்த அர்த்த மாகதி பாஷையானது, அனைவரது, ஆர்யர்களினதும் (உயர்ந்தோர்) அனார்யர்களினதும் (உயர்வற்றோர்) , சுயபாஷையாக பரிணமிக்கிறது.</p>

<p>ஆகவே, மஹாவீரர் மாகதி மொழியில் உபதேசிக்க, அந்த உபதேசங்கள் அனைத்து மக்களுடைய சொந்த மொழியில் மாற்றம் அடைகிறதாக கருதப்படுகிறது. இதே போன்றதொரு கருத்து பௌத்தர்களுக்கும் உண்டு. அதை பின்னர் காண்போம்.</p>

<p>11ஆம் நூற்றாண்டை சேர்ந்த ஜைன ஆச்சாரியர் ஒருவரான நமிசாது, சமஸ்கிருதம் பிராகிருதத்தில் இருந்தே தோன்றியது என்ற கருத்தை முன்வைக்கின்றார்.</p>

