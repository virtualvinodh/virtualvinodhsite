<p class="blockquote">[…] [பிராகிருதம்]  குழந்தைகள் பெண்கள் ஆகியோருக்கு எளிதாகவும் சகல பாஷைகளின் மூலமாகவும் கூறப்படுகிறது. மேகத்தில் இருந்து விடுவிக்கப்பட்ட நீர் (பின்னர் பலவடிவம் எடுப்பது) போல, (பிராகிருதம்) ஒரே சொரூபம் கொண்டிருந்து, அதுவே, தேச விதேச வேறுபாடுகளாலும் சமாஸம் முதலிய விசேஷங்களை பெற்று, சமஸ்கிருதம் முதலிய பிற மொழிகளுக்கான பேதத்தை அடைகிறது […]</p>

<p>இதில் இருந்து மிகத்தெளிவாக, ஜைனர்கள் தம்முடைய பிராகிருத மொழியான அர்த்த மாகதியையே சமஸ்கிருதம் உட்பட்ட அனைத்து மொழிகளுக்கும் மூல மொழியாக கருதினர் என்பது தெரிகிறது. அவர்களை பொருத்த வரையில், அனைத்து மொழிகளும் தமது தேவ பாஷையான அர்த்த மாகதி மொழியில் இருந்தே உருவானவை.</p>

<p class="text-h6">தேரவாத பௌத்தர்களின் மாகதி பிராகிருதம் (பாளி)</p>

<p>பழங்கால பௌத்தப்பிரிவுகளில் இன்றும் வழக்கில் இருக்கும் ஒரு பிரிவு தான் தேரவாதம் (சம்ஸ்: ஸ்தாவிரவாதம்). அனைத்து பௌத்த பிரிவுகளிலேயும் பிராகிருத மொழியான மாகதியை இன்று வரை பிராதனமாக கொண்டிருக்கும் ஒரே பிரிவு. (”பாளி” என்னும் சொல் உண்மையில் மாகதி மொழியில் உள்ள நூல்களை சுட்டுவது. ஏனோ, அது இன்று அந்த நூல்களின் மொழியை சுட்டுவதாகிவிட்டது ). தற்போது, தேரவாத பௌத்தம் இந்தியாவுக்கு வெளியே தான் பரவலாக இருந்தாலும், பாளி மொழியின் தாக்கம் இன்னும் அந்தந்த நாட்டு மக்களிடைய வெகுவாக காணப்படுகிறது. இதொன்றே, மாகதி மொழிக்கும் தேரவாதத்துக்கும் இருக்கும் பிணைப்பை வெளிப்படுத்துவதாக அமைகிறது.</p>
