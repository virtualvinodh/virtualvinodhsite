    <p>Thus spoke king Devanampiya Piyadasi : “Kings of the olden time have gone to heaven under these very desires. How then among mankind may religion (or growth in grace) be increased? Yea, through the conversion of the humbly-born shall religion increase”</p>
<p>
Thus spoke king Devanampiya Piyadasi : “The present moment and the past have departed under the same ardent hopes. How by the conversion of the royal-born may religion be increased? Through the conversion of the lowly-born if religion thus increaseth, by how much (more) through the conviction of the high-born, and their conversion, shall religion increase? Among whomsoever. the name of God resteth (?) verily this is religion, (or verily virtue shall there increase).”</p>
<p>
Thus spoke king Devanampiya Piyadasi : ” Wherefore from this very hour I have caused religious discourses to be preached; I have appointed religious observances that mankind having listened. Thereto shall be brought to follow in the right path and give glory unto God’” (Agni ?)</p>

